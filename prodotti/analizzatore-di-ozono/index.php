            <article id="FAQ">
              <h2>FAQ</h2>
              <dl class="accordion">
                <dt> Quali intervalli di misura sono disponibili per gli analizzatori di ozono? <span aria-hidden="true">&#10095;</span> </dt>
                <dd> I sensori di ozono sono disponibili nei seguenti intervalli: 0.05-0.2 ppm; 0.05-0.5 ppm; 0.05-2 ppm; 0.05-5 ppm; 0.05-10 ppm; 0-20 ppm (ppm o mg/l). </dd>
                <dt> È possibile misurare l'ozono nell'acqua di mare? <span aria-hidden="true">&#10095;</span></dt>
                <dd> Quando l'ozono è aggiunto all'acqua di mare, esso reagisce con il sale di bromuro presente nell'acqua e forma il bromo. In questi casi vi è bisogno di un sensore di <a href="<?php echo $root; ?>prodotti/analizzatore-di-cloro/">bromo totale (pagina del sensore di cloro)</a>, leggere la nota tecnica sulla <a href="<?php echo $root; ?>docs/note-tecniche/clorazione-acqua-di-mare.pdf">"Clorazione dell’acqua di mare"</a>, per comprendere la misura del bromo. </dd>
                <dt> Ogni quando bisogna calibrare il misuratore di ozono disciolto? <span aria-hidden="true">&#10095;</span></dt>
                <dd> Questo dipende dall'applicazione. I sensori di ozono residuo in linea hanno una deriva molto bassa: la maggior parte dei clienti lo calibra una volta a settimana o una volta al mese. </dd>
                <dt> Ogni quando bisogna cambiare l'elettrolita del sensore di ozono? <span aria-hidden="true">&#10095;</span></dt>
                <dd> Ogni 3-6 mesi. </dd>
                <dt> È possibile misurare l'ozono in presenza di tensioattivi (detergenti)? <span aria-hidden="true">&#10095;</span></dt>
                <dd> Sì, l'OzoSense è resistente agli effetti dei tensioattivi. </dd>
                <dt> Le variazioni del pH possono influire sulla lettura? <span aria-hidden="true">&#10095;</span></dt>
                <dd> No. </dd>
                <dt> Qual è la durata di conservazione delle membrane e dell'elettrolito? <span aria-hidden="true">&#10095;</span></dt>
                <dd> Se conservato in un luogo fresco ed asciutto, due anni. </dd>
                <dt> Quali sono le interferenze? <span aria-hidden="true">&#10095;</span></dt>
                <dd> Vi è una piccola risposta al cloro residuo (&lt; 2%) ed anche al biossido di cloro (&lt; 7%) per il sensore a membrana.  Potrebbero inoltre esserci altre interferenze, <a href="<?php echo $root; ?>azienda/contatti.php">contattare un consulente</a> per maggiori informazioni. </dd>
                <dt> Quali sono i materiali di costruzione del sensore? <span aria-hidden="true">&#10095;</span></dt>
                <dd> PVC, acciaio inossidabile, perspex (sensore a membrana e cella di flusso). </dd>
                <dt> Qual è il range di temperatura entro il quale il sensore può funzionare? <span aria-hidden="true">&#10095;</span></dt>
                <dd> &gt; 3 °C - &lt; 50 °C <br/>
                <dt> Perché non  vi è regolazione (calibrazione) dello zero sul sensore di ozono? <span aria-hidden="true">&#10095;</span></dt>
                <dd> Il sensore funziona sempre con una tensione positiva, quindi, qualsiasi deriva dello zero è trascurabile rispetto alla tensione operativa positiva, per questo non è necessario effettuare lo zero. </dd>
                <dt> Cosa accade al variare della temperatura del campione? <span aria-hidden="true">&#10095;</span></dt>
                <dd> Niente: i sensori sono dotati di un termistore che misura la temperatura ed esegue una compensazione automatica. </dd>
                <dt> Come calibrare il sensore? <span aria-hidden="true">&#10095;</span></dt>
                <dd> Per calibrare il sensore utilizzare un misuratore portatile. Questi sono disponibili da una varietà di fornitori e quasi tutti utilizzano il metodo <strong><abbr title="N, N-dietil-p-fenilendiammina">DPD</abbr> colorimetrico</strong> per determinare la concentrazione di ozono nel campione. </dd>
                <dt> A cosa fare attenzione quando si effettua un campionamento per effettuare un test DPD? <span aria-hidden="true">&#10095;</span></dt>
                <dd> Si deve prendere prima il campione d'acqua direttamente dallo strumento. Inoltre, è importante ricordare di non prendere il campione quando la concentrazione sta variando rapidamente. Infine, è fondamentale utilizzare un buon misuratore portatile e seguire attentamente le istruzioni. Scaricare e stampare la <a href="https://www.leafytechnologies.it/docs/note-tecniche/lista-controllo-DPD.pdf">Lista  di  controllo  per  il  metodo  DPD</a>. </dd>
                <dt> L'analizzatore dice che il campione non è stabile? <span aria-hidden="true">&#10095;</span></dt>
                <dd> Durante la calibrazione, l'analizzatore esamina la stabilità (velocità di cambiamento) del segnale dalla sonda e se varia di oltre il 10% durante il conto alla rovescia, l'analizzatore impedisce la calibrazione per evitare che la procedura di calibrazione introduca errori. </dd>
                <dt> Cos'è l'ozono? <span aria-hidden="true">&#10095;</span></dt>
                <dd> L'ozono è un allotropo di ossigeno con la formula chimica O<sub>3</sub>. Sono tre atomi di ossigeno uniti insieme. Quando decade forma O<sub>2</sub>. </dd>
                <dt> Perché l'ozono è usato per disinfettare l'acqua? <span aria-hidden="true">&#10095;</span></dt>
                <dd> L'ozono disciolto è un ossidante potente e può quindi essere usato per disinfettare l'acqua. Nello specifico può: reagire e rimuovere il manganese e distruggere alcuni agenti patogeni resistenti al cloro come <strong>giardia e cryptosporidium</strong>. </dd>
                <dt> Perché l'ozono è preferito in alcune applicazioni? <span aria-hidden="true">&#10095;</span></dt>
                <dd> L'ozono disinfetta e non lascia alcun sapore o odore. Inoltre, scompone completamente l'urea, quindi non forma organoclorurati o prodotti combinati di cloro. </dd>
                <dt> Qual è la differenza tra un misuratore di ozono, un analizzatore di ozono e una centralina per la misurazione dell'ozono? <span aria-hidden="true">&#10095;</span></dt>
                <dd> Non c'è differenza, sono solo tre espressioni utilizzate in modo intercambiabile. Un analizzatore di ozono è sistema con sensoristica ed elettronica integrate che permette di effettuare il controllo in maniera precisa. Gli analizzatori OzoSense permettono di effettuare un controllo di processo, ad esempio, controllando pompe di dosaggio, inviando allarmi e notifiche e comunicando con sistemi SCADA e PLC. </dd>
