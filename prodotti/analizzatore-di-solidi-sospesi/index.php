<p> All'interno del sensore di solidi sospesi SoliSense<sup>®</sup> vi è una sorgente luminosa e due rilevatori per effettuare le misure. Avere due rilevatori, posizionati a distanze diverse dalla sorgente luminosa, consente al sensore di essere utilizzato su di un <strong>ampio intervallo</strong> di concentrazioni di solidi sospesi. </p>
