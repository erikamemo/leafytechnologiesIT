  <article id="FAQ">
    <h2>FAQ</h2>
    <p> In questa sezione si riportano le risposte alle domande più frequenti sull'analizzatore di solidi sospesi </p>
    <dl class="accordion">
      <dt> Cosa sono i solidi sospesi? <span aria-hidden="true">&#10095;</span></dt>
      <dd> I solidi sospesi sono particelle sospese nell'acqua. Si presentano naturalmente e possono essere visti, ad esempio, nei fiumi dopo un evento di pioggia quando il fiume appare "fangoso". </dd>
      <dt> Come viene utilizzata la misura dei solidi sospesi nel trattamento delle acque e nel trattamento delle acque reflue? <span aria-hidden="true">&#10095;</span></dt>
      <dd> Il trattamento dell'acqua e delle acque reflue riguarda principalmente la rimozione di particelle e sostanze organiche e quindi la disinfezione dell'acqua. Essere in grado di <strong>misurare i solidi sospesi</strong> o la torbidità può aiutare a determinare il miglior trattamento, ad esempio, misurando i solidi sospesi nell'acqua grezza all'entrata di un impianto. <br/>
        Nel settore del trattamento delle acque reflue, i solidi sospesi, hanno nomi specifici come “Solidi Sospesi nella Miscela Liquida” (<abbr lang="en" title="Mixed liquor suspended solids">MLSS</abbr>) o “ricircolo dei fanghi attivi” (<abbr lang="en" title="Returned Activated Sludge">RAS</abbr> in inglese) ecc. <br/>
        Misurare i solidi sospesi utilizzando un analizzatore in linea in un depuratore può fornire informazioni importantissime sull'efficienza ed efficacia del processo di trattamento. </dd>
      <dt> Quali sono le unità di misura utilizzate dall'analizzatore di solidi sospesi? <span aria-hidden="true">&#10095;</span></dt>
      <dd> I solidi in sospensione sono tipicamente misurati in unità di massa su volume come, ad esempio, mg/l o mgl<sup>-1</sup>. A volte, quando i livelli sono piuttosto alti verranno misurati in g/l ed in %. </dd>
      <dt> Qual è la relazione tra solidi sospesi e torbidità? <span aria-hidden="true">&#10095;</span></dt>
      <dd> I solidi sospesi sono "unità di misura per volume", ad es. 1 mg di solidi sospesi in 1 litro d'acqua. La torbidità è una misura della "nuvolosità". Anche se ogni materiale sospeso avrà una relazione diversa (che cambierà nell'intervallo), una "regola generale" utile è che 1 mg/l = 2 NTU sebbene possa variare da 1 mg/l = 0.5 NTU a 1 mg/l = 2 NTU. </dd>
      <dt> Qual è il livello di solidi più alto che si può misurare con il sensore di solidi sospesi? <span aria-hidden="true">&#10095;</span></dt>
      <dd> Questo dipende interamente dal tipo di solidi. Ad esempio, se si misura sabbia (silice) in una sospensione di acqua pulita, è possibile misurare fino al 50% di solidi o 500,000 mg/l. Se, tuttavia, si misura fango nero in un impianto di trattamento delle acque reflue, può essere basso come il 5% o 50,000 mg/l. Tipicamente nella maggior parte dei depuratori ci si aggira intorno all'8% di sostanza secca o gli 80,000 mg/l. </dd>
      <dt> In che modo la temperatura influisce sul sistema? <span aria-hidden="true">&#10095;</span></dt>
      <dd> Ogni sensore ha una compensazione della temperatura incorporata in modo che l'effetto della temperatura sulla misurazione totale dei solidi sospesi sia trascurabile. Va notato però che il SoliSense<sup>®</sup> ha una massa importante che quindi può richiedere tempo per raggiungere l'equilibrio termico. Per questo motivo qualsiasi processo in cui la temperatura sta cambiando rapidamente  (variazioni maggiori di &gt; 2 °C per 10 minuti) potrebbe vedere un effetto della temperatura sulla lettura. </dd>
