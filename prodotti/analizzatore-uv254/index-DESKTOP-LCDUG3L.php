            <article id="FAQ">
              <h2>FAQ</h2>
              <dl class="accordion">
                <dt> Che cos'è la misura UV254? <span aria-hidden="true">&#10095;</span></dt>
                <dd> Facendo passare una luce con una particolare lunghezza d'onda (254 nm) attraverso l'acqua è possibile misurare quanta parte della luce viene assorbita dai <strong>contaminanti nell'acqua</strong>. Questa assorbanza può essere dovuta al particolato ed alle sostanze organiche che assorbono la luce a quella lunghezza d'onda. </dd>
                <dt> Come viene utilizzata la misura nel trattamento delle acque? <span aria-hidden="true">&#10095;</span></dt>
                <dd> Principalmente in due modi. Il primo è misurare la trasmissione della luce UV (<i lang="en">Ultra-Violet Transmittance</i>), questo è essenziale per determinare la potenza della luce UV utilizzata ai fini della disinfezione; il secondo è misurare l'assorbimento UV (<i lang="en">Ultra-Violet Absorbance</i>). L'UVA è una misurazione delle <strong>sostanze organiche</strong> presenti nell'acqua e può essere utilizzata come surrogato per il TOC, DOC, COD. L'UVA è più comunemente usata nel controllo della coagulazione negli impianti di trattamento delle acque. </dd>
                <dt> Qual è la relazione tra UVA e UVT? <span aria-hidden="true">&#10095;</span></dt>
                <dd> UVA = 2-log<sub>10</sub> UVT. <br/>
                  UVA e UVT sono essenzialmente la stessa misurazione espressa in modi diversi. 
                </dd>
                <dt> Qual è la relazione tra UV254 e colore? <span aria-hidden="true">&#10095;</span></dt>
                <dd> L'acqua grezza ha spesso un colore causato da sostanze organiche nell'acqua, quindi, sia UV che il colore possono essere usati come metodi per misurare la stessa cosa. I misuratori di colore sono utili per determinare <strong>l'accettabilità dell'aspetto</strong> dell'acqua potabile, ma molti elementi organici non ricadono all'interno dello spettro visibile (non hanno colore) e vi possono essere altre cause per il colore nell'acqua come, ad esempio, la presenza di ferro e manganese. La misurazione dell'UV254 è <strong>più sensibile</strong> ai composti organici aromatici nell'acqua che possono reagire con il cloro per produrre <abbr title="Disinfection By-prodcuts" lang="en">DBP</abbr> indesiderati (sottoprodotti della disinfezione) e di solito è vista come una misura più utile del colore, in particolare per il controllo della coagulazione. </dd>
                <dt> Come si compensa per la torbidità/particolato nell'acqua? <span aria-hidden="true">&#10095;</span></dt>
                <dd> L'analizzatore UV254Sense misura anche l'assorbanza ad un'altra lunghezza d'onda <strong> più vicina alla luce visibile </strong> dove i composti organici non mostrano assorbanza. Ciò consente al UV254Sense di compensare continuamente la torbidità nell'acqua. </dd>
                <dt> Qual è la bontà della correlazione tra UV254 e TOC? <span aria-hidden="true">&#10095;</span></dt>
                <dd> Il TOC è una misura difficile e costosa da realizzare in linea e l'UV254 può essere utilizzato in molti casi come misura sostitutiva. Ciò richiede che l'utente crei una <strong>correlazione</strong> tra il TOC e l'UV254 quando il sistema non è in linea e poi inserisca la relazione derivata tra i due nell'analizzatore; la misurazione verrà quindi riportata come TOC. La bontà di questa correlazione dipenderà dalla <strong> composizione del TOC</strong>. Se il TOC è costituito interamente da sostanze organiche assorbenti l'UV254, la correlazione sarà eccellente. In caso contrario, la correlazione diventerà prevedibilmente meno accurata. Il caso peggiore è quando il TOC è interamente composto da sostanze organiche non assorbenti e, in questo caso, la correlazione sarà pari a 0 (un esempio può essere lo zucchero). Tipicamente negli impianti di potabilizzazione ed in quelli di depurazione vi è una buona correlazione tra UV254 e TOC. </dd>
              </dl>
