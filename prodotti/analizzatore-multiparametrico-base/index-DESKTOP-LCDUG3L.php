            <article id="Applicazioni">
              <h2>Applicazioni</h2>
              <p> Il CRONOS<sup>®</sup> viene utilizzato in applicazioni dove non è necessaria la funzionalità di accesso remoto offerta dal <a href="<?php echo $root; ?>prodotti/analizzatore-multiparametrico-avanzato/">CRIUS<sup>®</sup></a> o dove vi è bisogno di un numero limitato di sensori. Il CRONOS<sup>®</sup> offre funzionalità di controllo come il PID, il controllo a modulazione di larghezza di impulso ed altri e viene spesso utilizzato in: </p>
              <ul>
                <li> <strong>trattamento acque primarie e secondarie</strong>; </li>
                <li> trattamento acque reflue; </li>
                <li> <strong>acqua potabile</strong>; </li>
                <li> acque reflue; </li>
                <li> controllo della piscina e di impianti termale/Spa; </li>
                <li> <strong>trattamento acque industriali</strong>; </li>
                <li> <strong>produzione di cibo e bevande</strong>; </li>
                <li> carta e polpa; </li>
                <li> <strong>torri di raffreddamento</strong> e </li>
                <li> qualsiasi applicazione di trattamento dell'acqua dove servono logiche di controllo, vari sensori ed opzioni avanzate. </li>
              </ul>
              <p> Il CRONOS<sup>®</sup> è un analizzatore universale in grado di emettere un segnale di controllo PID tramite un relè analogico (4-20 mA) o un relè a modulazione di larghezza d'impulso (per controllare, ad esempio, una pompa accesa/spenta). Il  CRONOS<sup>®</sup> supporta Profibus, Modbus TCP/IP via Ethernet, Modbus RTU ed altri protocolli su richiesta. </p>
              <p> La capacità di fornire promemoria di manutenzione è un’opzione interessante per i clienti di grosse dimensioni che hanno vari strumenti di cui occuparsi. </p>
              <p> Infine, i clienti che richiedono funzionalità aggiuntive come il registro dei dati scaricabile o l'<a href="<?php echo $root; ?>prodotti/accesso-remoto/">accesso remoto</a> via Internet dovrebbero prendere in considerazione l’<a href="<?php echo $root; ?>prodotti/analizzatore-multiparametrico-avanzato/">analizzatore multiparametrico avanzato </a>CRIUS<sup>®</sup>. </p>
