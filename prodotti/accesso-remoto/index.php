            <article id="Vantaggi">
              <h6>Tutorial</h6>
              <p> In questi tutorial si mostra come effettuare semplici operazioni utilizzando il sistema.
                Da notare è che l'interfaccia del Portale Cloud è in inglese ma, se richiesto prima
                dell'acquisto, sarà anche disponibile in lingua italiana. 
              </p>
              <h5>Esempio N. 1: Impostare allarmi e notifiche via SMS</h5>
              <ul>
                <li> Accedere al portale (<a href="https://portal.pi-remote.com" target="_blank"
                  title="Apre una nuova finestra" rel="nofollow">https://portal.pi-remote.com
                  </a>)
                  utilizzando l’e-mail <em>"public@pi.net"</em> e la password <em>"12345678"</em>.
                </li>
                <li> Selezionare l'analizzatore in modalità: Modem o Ethernet. </li>
                <li> A questo punto si giunge alla schermata principale dove sono riportati i parametri
                  misurati, ad esempio, <a href="<?php echo $root; ?>prodotti/analizzatore-di-ph/"
                    target="_blank">pH</a> e <a
                    href="<?php echo $root; ?>prodotti/analizzatore-di-cloro/"
                    target="_blank">cloro</a>. Questa pagina è chiamata <i lang="en">Dashboard</i>.
                </li>
                <li> Si supponga che la lettura del cloro sia 0.46 mg/l e che si desideri che il cloro
                  sia sempre inferiore a 0.45 mg/l (in questo tutorial si imposterà solo l'allarme di
                  alto livello). 
                </li>
                <li> Cliccare sul riquadro del sensore di cloro libero e selezionare <i lang="en">Alarm
                  1</i> o <i lang="en">Alarm 2</i>. Da questo menu, è possibile impostare
                  l'allarme. 
                </li>
                <li>
                  Cliccare su <i lang="en">Edit</i>, inserire le impostazioni seguenti e poi cliccare
                  su <i lang="en">Save</i>:
                  <ul>
                    <li> <strong> <i lang="en">Enabled</i> </strong>(attivato): spuntato </li>
                    <li> <strong> <i lang="en">Set Value</i> </strong>(valore obiettivo): 0.45 </li>
                    <li> <strong> <i lang="en">Reset Value</i> </strong>(valore di ripristino): 0.40
                    </li>
                  </ul>
                </li>
                <li> Con queste impostazioni, quando la lettura del cloro aumenta oltre 0.45 mg/l, il
                  sistema andrà in allarme, ed uscirà dall'allarme quando in livello tornerà al di
                  sotto di 0.40 mg/l. 
                </li>
                <li> Ora cliccare su <i lang="en">Menu</i> (nella parte superiore della pagina) e poi
                  selezionare il riquadro Modem. 
                </li>
                <li>
                  Selezionare <i lang="en">SMS Alerts</i>, inserire le impostazioni riportate di
                  seguito ed infine cliccare su <i lang="en">Save</i>:
                  <ul>
                    <li> <strong> <i lang="en">Enabled</i> </strong> (attivato): spuntato </li>
                  </ul>
                </li>
                <li> Selezionare <i lang="en">Phone Book</i> (Rubrica), quindi premere <i
                  lang="en">Edit</i> (Modifica). </li>
                <li> Ora inserire il proprio numero usando il seguente formato: +Codice paese - Numero
                  di telefono (ad esempio +39334789587854) e cliccare su <i lang="en">Save</i>. 
                </li>
                <li> Dopo qualche minuto, si riceverà un messaggio con la notifica di allarme. </li>
                <li> Per confermare la ricezione dell'allarme, inviare un messaggio di testo
                  all'analizzatore con il messaggio "OK". 
                </li>
                <li> Una volta che la notifica di ricevuta conferma è arrivata, cliccare <i
                  lang="en">Menu</i> e quindi <i lang="en">Refresh</i> (Aggiorna) dal <i
                  lang="en">Dashboard</i>. </li>
                <li> Il riquadro <i lang="en">Analyser</i> (Analizzatore) ora dovrebbe essere giallo per
                  indicare che l'allarme è stato confermato. 
                </li>
                <li> Dopo aver ricevuto il messaggio, si consiglia di cancellare il proprio numero (<i
                  lang="en">Modem > Phone Book > Edit > Delete Number > Save</i>) e di disattivare
                  l'allarme (<i lang="en">Free Chlorine > Alarm 1 (or Alarm 2) > Enabled: no >
                  Save</i>). 
                </li>
              </ul>
              <h5>Esempio N. 2 – effetuare il download dei dati</h5>
              <p> Nell'esempio seguente si mostra come effetuare il download del <strong>registro dei dati e di
                stato</strong>: 
              </p>
              <ul>
                <li> Accedere al portale (<a href="https://portal.pi-remote.com" target="_blank"
                  title="Apre una nuova finestra" rel="nofollow">https://portal.pi-remote.com
                  </a>)
                  utilizzando l’e-mail <em>"public@pi.net"</em> e la password <em>"12345678"</em>.
                </li>
                <li> Selezionare l'analizzatore in modalità: Modem o Ethernet. </li>
                <li> A questo punto si giunge alla schermata principale dove sono riportati i parametri
                  misurati, ad esempio, <a href="<?php echo $root; ?>prodotti/analizzatore-di-ph/"
                    target="_blank">pH</a> e <a
                    href="<?php echo $root; ?>prodotti/analizzatore-di-cloro/"
                    target="_blank">cloro</a>. Questa pagina è chiamata <i lang="en">Dashboard</i>.
                </li>
                <li> Selezionare <i lang="en">Data</i> sulla sinistra. È possibile <strong>modificare
                  l'intervallo dati</strong> in alto a sinistra. 
                </li>
                <li> Le impostazioni presenti sotto al grafico consentono di modificare sia le
                  informazioni visualizzate sia come queste vengono presentate. 
                </li>
                <li> Provare ad abilitare o disabilitare alcuni parametri ed a modificare alcuni
                  intervalli. Il <strong> grafico si aggiornerà in tempo reale</strong>. 
                </li>
                <li> Per effetuare il download di un'immagine del grafico, cliccare su <i lang="en">Download Image</i>.
                  Se si desidera effetuare il download dei dati, cliccare su <i lang="en">Download CSV</i>. 
                </li>
                <li> Per effetuare il download dei registri di stato, selezionare <i lang="en">Registers</i> a
                  sinistra, quindi premere <i lang="en">Download CSV</i>. L'intervallo di dati può
                  essere modificato nella parte superiore sinistra della pagina. 
                </li>
              </ul>
              <h5>Esempio N. 3 - Modificare le impostazioni PID</h5>
              <p> La procedura seguente mostra come modificare la<strong> modalità di esecuzione, il
                valore manuale, il valore obiettivo del PID ed i valori di <i lang="en">gain</i>
                </strong>(guadagno): 
              </p>
              <ul>
                <li> Accedere al portale (<a href="https://portal.pi-remote.com" target="_blank"
                  title="Apre una nuova finestra" rel="nofollow">https://portal.pi-remote.com
                  </a>)
                  utilizzando l’e-mail <em>"public@pi.net"</em> e la password <em>"12345678"</em>.
                </li>
                <li> Selezionare l'analizzatore in modalità: Modem o Ethernet. </li>
                <li> A questo punto si giunge alla schermata principale dove sono riportati i parametri
                  misurati, ad esempio, <a href="<?php echo $root; ?>prodotti/analizzatore-di-ph/"
                    target="_blank">pH</a> e <a
                    href="<?php echo $root; ?>prodotti/analizzatore-di-cloro/"
                    target="_blank">cloro</a>. Questa pagina è chiamata <i lang="en">Dashboard</i>.
                </li>
                <li> Scarica le ultime impostazioni premendo <i lang="en">Refresh</i> (Aggiorna). </li>
                <li> Selezionare il riquadro <i lang="en"> PID Control (Cl)</i>. </li>
                <li> Per cambiare la modalità di esecuzione, selezionare <i lang="en">Set control</i>,
                  quindi fare clic su <i lang="en">Edit</i>. Da qui è possibile modificare la modalità
                  su "Off / Manuale / Automatico / Semi automatico". Quando selezionato, premere <i
                    lang="en">Save</i> per chiudere la finestra delle impostazioni. 
                </li>
                <li> Per modificare il <strong>valore manuale</strong>, selezionare <i lang="en">Output
                  Setup: Primary</i>, quindi cliccare su <i lang="en">Edit</i>, inserire il nuovo
                  valore manuale ed infine <i lang="en">Save</i>. 
                </li>
                <li> Per modificare il <strong> valore obiettivo PID</strong>, selezionare <i lang="en">
                  Setup: Setpoint</i>, quindi cliccare su <i lang="en">Edit</i>, inserire il nuovo
                  valore ed infine <i lang="en">Save</i>. 
                </li>
                <li> Per modificare il <strong> valore di guadagno</strong>, selezionare <i lang="en">
                  Setup: PID Tuning</i>, quindi cliccare su <i lang="en">Edit</i>, inserire il
                  nuovo valore ed infine <i lang="en">Save</i>. 
                </li>
