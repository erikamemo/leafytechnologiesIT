            <article id="FAQ">
              <h2>FAQ</h2>
              <!-- <span aria-hidden="true">&#10095;</span> -->
              <p> In questa sezione si riportano le risposte alle domande più frequenti sull'analizzatore di cloro. </p>
              <dl class="accordion">
                <dt> Cosa è la misura della conducibilità in una soluzione acquosa? <span aria-hidden="true">&#10095;</span></dt>
                <dd> La conducibilità è la <strong> misura delle specie ioniche</strong> all'interno di una soluzione. È definita come la conduttanza in un determinato volume. La conduttanza è la capacità della soluzione di condurre la corrente elettrica. </dd>
                <dt> Come sono correlate conducibilità e conduttanza? <span aria-hidden="true">&#10095;</span></dt>
                <dd> La conduttanza è la <strong>capacità di una soluzione di condurre la corrente elettrica</strong>, mentre la conducibilità <strong>è la conduttanza in un dato volume</strong> (solitamente misurata in μΩ/cm o μS/cm). </dd>
                <dt> In che modo la temperatura influisce sulle letture della conduttività (conducibilità)? <span aria-hidden="true">&#10095;</span></dt>
                <dd> La temperatura influisce sulla misura della conduttività. Per questo motivo  tutti i sensori di conducibilità ConductiSense hanno un termistore integrato e forniscono una compensazione automatica della temperatura. </dd>
                <dt> La conducibilità può essere misurata solo in soluzioni acquose? <span aria-hidden="true">&#10095;</span></dt>
                <dd> No, tutte le sostanze possiedono alcune proprietà conduttive. Generalmente composti  organici (come
                  benzene, alcoli e prodotti petroliferi) hanno valori di conducibilità molto bassi, mentre i metalli hanno conducibilità molto elevate. Misurare la conduttività di liquidi altamente infiammabili è generalmente rischioso. 
                </dd>
                <dt> Cos'è una costante di cella e perché ve ne sono diverse? <span aria-hidden="true">&#10095;</span></dt>
                <dd> La costante di cella, <var>K</var>, è uguale all'area (<var>a</var>) normale al flusso corrente in centimetri quadrati divisa per la distanza in centimetri tra gli elettrodi (<var>d</var>). Per soluzioni con basse conduttività, gli elettrodi possono essere posizionati più vicini o ingranditi in modo che la costante di cella sia inferiore ad uno. Ciò ha l'effetto di <strong>aumentare la conduttanza e produrre un valore più facilmente misurabile dall'elettronica</strong>. Il contrario si applica anche, in soluzioni ad alta conduttività, gli elettrodi sono posizionati più distanti o resi più piccoli. Diverse costanti di cella vengono utilizzate come moltiplicatori di intervallo. Il valore standard <var>K</var> è 1 con un valore <var>K</var> di 0.1 utilizzato per acqua a bassa conduttività e un valore <var>K</var> di 10 per acqua a conducibilità più elevata. </dd>
                <dt> Come sono correlati conduttività e solidi totali disciolti (TDS)? <span aria-hidden="true">&#10095;</span></dt>
                <dd> I solidi totali disciolti in una soluzione (<abbr lang="en" title="Total Dissolved Solids">TDS</abbr>) contribuiscono alla conduttività di tale soluzione. La maggior degli operatori utilizzano un moltiplicatore standard per convertire la conduttività in TDS (in genere il fattore di moltiplicazione è 0.65). Questo moltiplicatore funziona per soluzioni con NaCl. Soluzioni diverse avranno moltiplicatori diversi. </dd>
                <dt> Come calibrare lo strumento quando i solidi totali disciolti nel campione sono diversi da quelli negli standard di calibrazione in commercio? <span aria-hidden="true">&#10095;</span></dt>
                <dd> In questi casi, produrre un proprio standard di calibrazione produrrà risultati più accurati. Questo viene fatto creando una miscela di sali in proporzioni relative che simulano la soluzione da testare, quindi sciogliendo la miscela in acqua distillata. Questo dovrebbe essere fatto seguendo la formula: <br/>
                  <br/>
                  <strong> 1 mg di miscela di sale/litro di acqua distillata = 1 ppm di TDS</strong> <br/>
                  <br/>
                  o anche <br/>
                  <br/>
                  <strong> X ppm di TDS = X mg di sali + 1 litro di acqua distillata.</strong> <br/>
                  <br/>
                  Bisogna ricordare che "X mg di sali" è il numero di milligrammi di una miscela di sale le cui proporzioni simulano la tua soluzione di prova. Un valore appropriato per "X" è determinato dalla seguente regola: <br/>
                  "Scegliere un valore ppm per una soluzione calibrata il più vicino possibile ai valori ppm previsti delle soluzioni di test. Se si prevede che il contenuto in ppm della soluzione di test varierà molto, è preferibile scegliere un valore ppm per la soluzione calibrata che si trovi nel <strong>terzo superiore</strong> del campo di conducibilità in TDS". 
                </dd>
                <dt> Qual è la differenza tra micro-ohm  (μΩ/cm) e microsiemens (μS/cm)? <span aria-hidden="true">&#10095;</span></dt>
                <dd> Non c'è differenza. micro-ohm (μΩ/cm) è più comunemente usato negli Stati Uniti mentre, microsiemens (μS/cm) è più diffuso in Europa. </dd>
                <dt> Come pulire l'elettrodo di conducibilità? <span aria-hidden="true">&#10095;</span></dt>
                <dd> Pulire gli elettrodi con un detergente liquido delicato e/o acido nitrico diluito (0.1 M) immergendo il sensore nella soluzione e agitandolo per 2 o 3 minuti. Si può anche usare HCl diluito (acido cloridrico) o H<sub>2</sub>SO<sub>4</sub> (acido solforico). </dd>
                <dt> Come conservare l'elettrodo di conducibilità? <span aria-hidden="true">&#10095;</span></dt>
                <dd> Dopo l'utilizzo, risciacquare con acqua pulita. L'elettrodo può essere conservato sia bagnato che asciutto. Se conservato asciutto, è necessario ricondizionare l'elettrodo prima dell'uso. </dd>
                <dt> Come fare per condizionare o ricondizionare una sonda? <span aria-hidden="true">&#10095;</span></dt>
                <dd> Posizionare la sonda in una soluzione standard o in acqua di rubinetto con la sonda connessa all'analizzatore e l'analizzatore acceso. Lasciare la sonda nella soluzione per un periodo che varia tra i 30 minuti ad 1 ora, se non diversamente specificato. </dd>
                <dt> Qual è la differenza tra conduttività e salinità? <span aria-hidden="true">&#10095;</span></dt>
                <dd> La sonda è la stessa sia per la misura della conduttività che per la misura della salinità; la differenza è che in un misuratore di salinità viene applicato un <strong> fattore di correzione</strong> alla lettura. Il fattore di correzione prende la lettura della conducibilità e la converte in ppm di un sale specifico. Il sale varia da produttore a produttore di soluzioni standardizzate. Alcuni usano NaCl mentre altri usano CaCO<sub>3</sub>. </dd>
                <dt> È possibile utilizzare sensori esistenti sugli analizzatori Pi? <span aria-hidden="true">&#10095;</span></dt>
                <dd> Di solito è possibile. </dd>
                <dt> Come e quando bisogna calibrare la sonda? <span aria-hidden="true">&#10095;</span></dt>
                <dd> Calibrare utilizzando una soluzione standard il cui valore sia all'interno dell'intervallo che si sta testando. Posizionare la sonda all'interno della soluzione standard, condizionare, risciacquare la sonda in un secondo campione di soluzione standard, utilizzare un terzo campione di soluzione standard per calibrare e quindi regolare l'analizzatore finché non venga visualizzato un valore specificato. Ricalibrare quando si cambiano gli intervalli o se le letture sembrano non essere corrette. </dd>
                <dt> Come scegliere l'intervallo per un sensore di conducibilità? <span aria-hidden="true">&#10095;</span></dt>
                <dd> I due tipi di sensori di conduttività principali sono il sensore toroidale e quello in grafite (ve ne è un terzo per applicazioni in caldaie industriali). Il sensore <strong>toroidale</strong> copre l'intervallo di misura da 0 μS/cm a 2,000,000 μS/cm (ossia 0 mS/cm a 2,000 mS/cm). Il sensore in <strong>grafite</strong> copre l'intervallo di misura 0-5,000 μS/cm. All'interno di tale intervallo si utilizzano diverse costanti di intervallo (fattore K) per rendere il sensore più adatto a diverse applicazioni. Se, ad esempio, si vuole misurare nell'intervallo 0-100 μS/cm, si avrà bisogno di un fattore K di 0.1. Se invece si misura nell'intervallo 0-1,000 μS/cm, allora un fattore K di 1 dovrà essere applicato. </dd>
              </dl>
