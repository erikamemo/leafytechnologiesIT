          <h2>Introduzione</h2>
          <p> La linea di analizzatori e sensori per la misurazione del fluoro in continuo utilizza alcuni tra i migliori sensori esistenti al mondo per misurare il contenuto di <strong>fluoro in acqua potabile</strong>. </p>
          <p> Questi sensori sono elettrodi iono-selettivi (<abbr lang="en" title="Ion Selective Electrode">ISE</abbr>) con riferimento integrale, compensazione automatica della temperatura e giunzione polimerica solida. Questi sistemi non utilizzano reagenti, sono <strong>estremamente stabili</strong> ed hanno una manutenzione e costi di proprietà ridotti. </p>
