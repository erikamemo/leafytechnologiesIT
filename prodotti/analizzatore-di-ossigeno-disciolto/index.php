          <section>
            <h3>Principio di funzionamento </h3>
            <p> Quasi tutti gli analizzatori di fluoro utilizzano <strong>elettrodi iono-selettivi</strong> di fluoro. In alcuni sistemi si aumenta in livello di accuratezza dosando in continuo uno stabilizzatore ionico nel campione, generalmente noto come <dfn><abbr lang="en" title="Total Ionic Strength Adjustment Buffer">TISAB</abbr>: tampone di aggiustamento della forza ionica totale</dfn>. </p>
            <figure style="float: left; clear: right; margin-right: 16px;">
              <picture>
                <source type="image/webp" srcset="<?php echo $root; ?>images/pi-prodotti/sensore-fluoro-cella-flusso-doppia.webp">
                <img src="<?php echo $root; ?>images/pi-prodotti/sensore-fluoro-cella-flusso-doppia.jpg" alt="Sensore di fluoro in cella di flusso"/> </picture>
              <figcaption style="text-align: left;"> Cella di flusso </figcaption>
            </figure>
            <p> Questi tamponi aiutano a "fissare" la forza ionica di una soluzione ma, nel far ciò, introducono un costo ed una complessità che sono considerati da molti non necessari nel controllo del <strong>dosaggio del fluoro</strong> in acqua potabile. </p>
            <p> L'analizzatore di fluoro FluoriSense non utilizza il metodo TISAB e, pertanto, è adatto per l'utilizzo in applicazioni in cui la forza ionica dell'acqua è relativamente stabile e non si ha bisogno di un livello di accuratezza elevatissimo. </p>
            <p> In molte applicazioni l'analizzatore di fluoro viene impiegato principalmente per garantire che il dosaggio del fluoro (proporzionale al flusso) stia funzionando correttamente e, in queste applicazioni, FluoriSense è il sistema ideale che fornisce una soluzione affidabile a <strong>costi contenuti</strong>, facile da installare e monitorare. </p>
            <p> Considerando le funzionalità aggiuntive che questo analizzatore di fluoro supporta, risulta essere una soluzione ottimale rispetto a quelle tradizionali. </p>
          </section>
          
