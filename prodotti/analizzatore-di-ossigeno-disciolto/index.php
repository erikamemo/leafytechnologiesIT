          <h2>Opzioni</h2>
          <p> L'analizzatore di cloro HaloSense può essere integrato con una serie di opzioni addizionali. Di seguito si riporta una descrizione dettagliata delle opzioni più comuni. </p>
          <section>
            <h3>Autopulizia</h3>
            <p> I sensori di fluoro FluoriSense possono essere equipaggiati di un sistema di pulizia automatico che permette di pulire i sensori ad intervalli definiti dall'utente. Grazie a questo sistema, il sensore può funzionare <strong>a lungo senza manutenzione</strong>. </p>
            <p> L'autopulizia è particolarmente utile negli impianti dove è necessario un alto livello di automatizzazione. Visitare <a href="<?php echo $root; ?>prodotti/sistema-di-autopulizia/">la pagina del sistema AutoFlush per maggiori informazioni</a>. </p>
            <p>A destra un video con una dimostrazione del sistema AutoFlush. </p>
          </section>
          <section>
            <h3>Protocolli di comunicazione</h3>
            <p> Gli analizzatori di fluoro CRONOS<sup>®</sup> e CRIUS<sup>®</sup> FluoriSense (analizzatore + sensore) possono essere dotati di  controlli PID, registro dati, uscite relè, uscite analogiche e comunicazioni seriali quali: <strong> Ethernet, Modbus e Profibus</strong>. Il monitoraggio e la <a href="<?php echo $root; ?>prodotti/accesso-remoto/">telegestione da remoto</a> degli strumenti è disponibile via Internet tramite GPRS e tramite LAN. L'analizzatore CRIUS<sup>®</sup> HaloSense offre le opzioni di controllo e comunicazione più avanzate, mentre il CRONOS<sup>®</sup> offre un'alternativa semplice senza intaccare la qualità della misura (la quale dipende dal sensore HaloSense). <br/>
            </p>
          </section>
          <section>
            <h3>Tipologie di controllo</h3>
            <p> Ogni analizzatore di fluoro della <i lang="en">Process Instruments </i> può supportare più canali di controllo che possono gestire il dosaggio chimico. Questo viene effettuato attraverso un relè, attivando il dosaggio quando il fluoro è troppo basso e disattivandolo quando è troppo alto, o attraverso un controllo PID quando si vuole ottenere un controllo di processo molto più raffinato. </p>
            <p> <dfn>PID</dfn> significa <strong>Proporzionale-Integrale-Derivativo</strong> ed è una <strong> manipolazione matematica</strong> del segnale del sensore che fornisce un segnale variabile attraverso il quale si può controllare una pompa ed ottenere un livello costante di fluoro nell'acqua. Tutte le funzioni sono regolabili e nel sistema sono anche incorporate funzioni di sicurezza come la protezione da sovralimentazione. Per una discussione sui tipi di controllo, consultare la nota tecnica <a href="<?php echo $root; ?>docs/note-tecniche/tipi-controllo-analizzatori-acqua.pdf">"Tipi di controllo negli analizzatori multiparametrici"</a>. </p>
