          <p> La natura modulare del sistema FluoriSense (sensore + analizzatore) permette di acquistare soltanto le opzioni richieste, senza perdere la capacità di integrare funzionalità aggiuntive qualora fossero necessarie in futuro. Se il progetto richiede un sistema semplice, conviene orientarsi verso <strong><a href="<?php echo $root; ?>prodotti/analizzatore-multiparametrico-base/">l'analizzatore multiparametrico di base CRONOS<sup>®</sup></a></strong>, altrimenti, se vi è la necessità di monitorare vari parametri (fino a 16), gestire vari segnali provenienti da strumenti diversi ed effettuare la telegestione via internet del sistema, si consiglia di orientarsi verso <strong><a href="<?php echo $root; ?>prodotti/analizzatore-multiparametrico-avanzato/">l'analizzatore multiparametrico avanzato CRIUS<sup>®</sup></a></strong>. </p>
