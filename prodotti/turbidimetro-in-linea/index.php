              Esistono due modi per calibrare un sensore e TurbSense<sup>®</sup> non fa eccezione. <br>
              Il primo consiste nell'utilizzare un altro dispositivo, ad esempio, un misuratore di torbidità portatile palmare per determinare la torbidità del campione monitorato da TurbSense<sup>®</sup> . <br>
              Il secondo è inserire il TurbSense<sup>®</sup> in un campione con una torbidità nota. <br>
              Normalmente si dovrebbe anche inserire l'altro sensore di torbidità in uno "zero" per dare al sensore due punti per disegnare una calibrazione, ma il TurbSense<sup>®</sup> utilizza alcune leggi fondamentali dell'ottica per rendere lo zero inutile (sistema in fase di brevettamento). </dd>
            <dt> Perché la parte ottica del sensore è fatta in zaffiro? <span aria-hidden="true">&#10095;</span></dt>
            <dd> Lo zaffiro è utilizzato poiché è resistente ai graffi (molto più del vetro o del quarzo). Il suo uso consente anche di avere una parte ottica più sottile (perché è più forte) e quindi avere una buona trasmissione ottica. </dd>
            <dt> Quel è la temperatura massima alla quale si può utilizzare il sistema TurbSense<sup>®</sup> ? <span aria-hidden="true">&#10095;</span></dt>
            <dd> La temperatura massima del campione monitorato è 50 °C. Si è scelto 50 °C perché è ben entro i limiti superiori dei componenti elettronici che usiamo e offre comunque un buon intervallo di temperatura per misurare la torbidità. </dd>
            <dt> È possibile installare il sensore direttamente in una tubazione? <span aria-hidden="true">&#10095;</span></dt>
            <dd> Si può fare se la torbidità è abbastanza alta o il tubo è abbastanza grande. Per ottenere un alto livello di prestazioni, TurbSense<sup>®</sup> utilizza una  sorgente luminosa potente ed un'ottica sensibile. Ciò significa che in un tubo, i riflessi da pareti e pieghe possono causare letture imprecise. Maggiore è la torbidità, più le superfici riflettenti saranno vicine così che a &gt; 200 NTU il misuratore di torbidità TurbSense<sup>®</sup> in un tubo non dovrebbe costituire un problema. </dd>
            <dt> A che altro bisogna pensare se si sta pensando di acquistare un TurbSense<sup>®</sup> ? <span aria-hidden="true">&#10095;</span></dt>
            <dd> Per prestazioni ottimali, scegliere anche l'opzione di autopulizia  automatica del sensore. Questo può essere usato per pulire periodicamente la finestra del sensore per garantire la massima precisione nelle letture. Cercare di evitare di posizionare il sensore alla luce diretta del sole poiché l'ottica sensibile può rilevare la luce a infrarossi emessa dal sole. </dd>
            <dt> Qual è la funziona della cella di flusso per un TurbSense<sup>®</sup> ? <span aria-hidden="true">&#10095;</span></dt>
            <dd> La cella di flusso rimuove le bolle trascinate per evitare che causino un'interferenza alle misurazioni. La sua struttura in polipropilene nero elimina la luce diffusa e il coperchio rimuove la luce ambientale, entrambe potenziali fonti di interferenza nelle misurazioni. Per maggiori dettagli, si prega di consultare la  nota tecnica <a href="<?php echo $root; ?>docs/note-tecniche/bolle-nella-misura-torbidita.pdf">"Bolle e torbidità"</a>. </dd>
