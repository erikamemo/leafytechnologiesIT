                <h3>Limitazioni delle soluzioni tradizionali </h3>
                <h4>Soluzioni tampone </h4>
                <p> Gli operatori degli impianti di trattamento delle acque si stanno velocemente
                  distanziando dai sistemi tradizionali di misura del cloro che utilizzano tamponi di
                  acetato e fosfato poiché risultano costosi e non particolarmente rispettosi
                  dell'ambiente. Questi sistemi richiedono inoltre <strong>una manutenzione
                  elevata</strong> e materiali di consumo costosi. Vi sono inoltre considerazioni
                  sulla salute e sulla sicurezza nella gestione degli acidi, così come costi di
                  smaltimento non indifferenti quando, ad esempio, l'acqua trattata con acido non può
                  essere reimmessa nella rete idrica. 
                </p>
                <h4>Celle amperometriche e sistemi polarografici </h4>
                <p> Le celle amperometriche e la maggior parte delle sonde polarografiche rispondono
                  <strong>solo all'acido ipocloroso (HOCl)</strong>. L'acido ipocloroso (HOCl) si
                  dissocia in ione ipoclorito (OCl<sup>–</sup>) al variare del pH. Questo è il motivo
                  per cui la maggior parte degli analizzatori di cloro hanno bisogno di tamponi acidi.
                  Il pH tipico dell'acqua misurato in una stazione di trattamento può variare tra pH 7
                  e pH 9.2. 
                </p>
                <figure style="display: block; width: 100%; text-align: center; margin-top: 16px;">
                  <picture>
                    <source type="image/webp"
                      srcset="<?php echo $root; ?>/images/pi-approfondimenti/tipica-risposta-ph-sensore-di-cloro.webp">
