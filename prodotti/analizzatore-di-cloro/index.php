              <h2>FAQ</h2>
              <p> In questa sezione si riportano le risposte alle domande più frequenti sull'analizzatore
                di cloro. 
              </p>
              <dl class="accordion">
                <dt>Cosa significa "cloro residuo"?<span aria-hidden="true">&#10095;</span></dt>
                <dd> Quando il cloro viene aggiunto come disinfettante nell'acqua, esso ossida il
                  materiale presente nell'acqua, uccidendo quindi tutti gli organismi. Il "cloro
                  residuo" è il cloro rimasto alla fine di questo processo ed è solitamente quello che
                  si misura. Questo dato è importante perché da un'idea di quanto potenziale di
                  disinfezione è ancora presente nell'acqua. 
                </dd>
                <dt> Cos'è il cloro "libero"? <span aria-hidden="true">&#10095;</span></dt>
                <dd> Il cloro libero è il cloro presente nell'acqua come HOCl or OCl<sup>–</sup>. <br />
                  Quando il cloro viene aggiunto nell'acqua pura con un pH tra pH 4 e pH 11: <br />
                  Cl<sub>2</sub> + OH<sup>–</sup> ↔ HOCl + Cl<sup>–</sup> <br />
                  HOCl ↔ OCl<sup>–</sup> + H<sup>+</sup> <br />
                  quindi se il cloro viene aggiunto all'acqua si ottiene HOCl (acido ipocloroso) e
                  OCl<sup>–</sup> (ione ipoclorito), che insieme producono il "cloro libero". 
                </dd>
                <dt> Cos'è il cloro "combinato"? <span aria-hidden="true">&#10095;</span></dt>
                <dd> Se l'acqua contiene sia ammoniaca che ipoclorito, reagirà formando monocloramina.
                  <br />
                  NH<sub>3</sub> + OCl<sup>–</sup> → NH<sub>2</sub>Cl + OH<sup>–</sup> <br />
                  In una soluzione acida, la monocloramina si sproporziona per formare il tricloruro
                  di azoto. <br />
                  2NH<sub>2</sub>Cl + H<sup>+</sup> → NHCl<sub>2</sub> + NH<sub>4</sub><sup>+</sup>
                  <br />
                  3NHCl<sub>2</sub> + H<sup>+</sup> → 2NCl<sub>3</sub> + NH<sub>4</sub><sup>+</sup>
                  <br />
                  In soluzioni dove vi sono basse concentrazioni di cloro, spesso l'odore che si sente
                  è quello delle cloroammine e non del "cloro". Le tre cloroammine sopra sono
                  conosciute collettivamente come "cloro combinato". 
                </dd>
                <dt> Cos'è il cloro "totale"? <span aria-hidden="true">&#10095;</span></dt>
                <dd> Il cloro totale è la combinazione di cloro libero e cloro combinato. </dd>
                <dt> Quali intervalli di misura sono disponibili? <span
                  aria-hidden="true">&#10095;</span></dt>
                <dd> I sensori di cloro libero e totale sono disponibili nell'intervallo 0.005-0.5 ppm
                  (solo totale); 0.005-2 ppm; 0.05-5 ppm; 0.05-10 ppm; 0.05-20 ppm; 0.5-200 ppm (solo
                  libero). 
                </dd>
                <dt> È possibile misurare il cloro in acqua di mare? <span
                  aria-hidden="true">&#10095;</span></dt>
                <dd> Sì, ma quando si aggiunge il cloro nell'acqua di mare si verifica una reazione di
                  spostamento per formare il bromo residuo. Per ulteriori informazioni consultare la
                  nota tecnica <a
                    href="<?php echo $root; ?>docs/note-tecniche/clorazione-acqua-di-mare.pdf">"Clorazione
                  dell’acqua di mare".</a> 
                </dd>
                <dt> Ogni quanto bisogna calibrare il sensore? <span aria-hidden="true">&#10095;</span>
                </dt>
                <dd> Questo dipende dall'applicazione. Il sensore di cloro in linea ha una deriva molto
                  bassa quindi, la maggior parte dei clienti, lo calibra una volta al mese ma si
                  arriva anche ad utilizzarlo per sei mesi di seguito prima di ricalibrarlo. 
                </dd>
                <dt> Ogni quanto bisogna cambiare l'elettrolita? <span
                  aria-hidden="true">&#10095;</span></dt>
                <dd> Una volta all'anno (sensore di cloro libero e totale); ogni 3-6 mesi (sensore cloro
                  zero). 
                </dd>
                <dt> Ogni quanto bisogna cambiare la membrana del sensore? <span
                  aria-hidden="true">&#10095;</span></dt>
                <dd> Una volta all'anno. </dd>
                <dt> Il cambiamento del pH influisce sulla lettura? <span
                  aria-hidden="true">&#10095;</span></dt>
                <dd> Sì, ma poco e nella maggior parte delle applicazioni è accettabile. Maggiori
                  informazioni si trovano nella nota tecnica <a
                    href="<?php echo $root; ?>docs/note-tecniche/effetti-variazione-ph-sensore-cloro.pdf">"Effetti
                  della variazione del pH sui sensori amperometrici di cloro"</a>. 
                </dd>
                <dt> Quali sono le interferenze note? <span aria-hidden="true">&#10095;</span></dt>
                <dd> Sia l'ozono che il biossido di cloro sono interferenti noti. </dd>
                <dt> Qual è la durata di conservazione delle membrane e dell'elettrolito? <span
                  aria-hidden="true">&#10095;</span></dt>
                <dd> Se conservato in un luogo fresco e asciutto, due anni. </dd>
                <dt> Quali sono i materiali di costruzione? <span aria-hidden="true">&#10095;</span>
                </dt>
                <dd> PVC-U, acciaio inossidabile, membrana idrofila, <abbr
                  title="polietere etere chetone">PEEK</abbr> (totale e zero) e silicone. </dd>
                <dt> Qual è il range di temperatura del sensore? <span
                  aria-hidden="true">&#10095;</span></dt>
                <dd> 0- 45 °C (libero e totale), 0 - 40 °C (zero). </dd>
                <dt> Perché non serve la calibrazione dello zero? <span
                  aria-hidden="true">&#10095;</span></dt>
                <dd> Il sensore funziona sempre con una tensione positiva quindi, qualsiasi deriva sullo
                  zero è trascurabile rispetto alla tensione operativa positiva, ciò significa che non
                  è necessario effettuare lo zero. 
                </dd>
                <dt> Vi sono effetti sul sensore al variare della temperatura? <span
                  aria-hidden="true">&#10095;</span></dt>
                <dd> No: il sensore ha un termistore che misura la temperatura ed esegue una
                  compensazione automatica. 
                </dd>
                <dt> Come calibrare il sensore? <span aria-hidden="true">&#10095;</span></dt>
                <dd> Utilizzare un misuratore portatile. Questi sono disponibili da vari fornitori e
                  quasi tutti utilizzano il metodo <abbr lang="en"
                    title="N,N-diethyl-p-phenylenediamine">DPD</abbr> colorimetrico per
                  determinare la concentrazione di cloro nel campione. 
                </dd>
                <dt> A cosa bisogna fare attenzione quando si effettua un test DPD? <span
                  aria-hidden="true">&#10095;</span></dt>
                <dd> Si deve prendere prima il campione d'acqua direttamente dallo strumento. Inoltre, è
                  importante ricordare di non prendere il campione quando la concentrazione sta
                  variando rapidamente. Infine, è fondamentale utilizzare un buon misuratore portatile
                  e seguire attentamente le istruzioni. Scaricare e stampare la <a
                    href="https://www.leafytechnologies.it/docs/note-tecniche/lista-controllo-DPD.pdf">Lista
                  di controllo per il metodo DPD</a>. 
                </dd>
                <dt> L'analizzatore dice che il campione non è stabile? <span
                  aria-hidden="true">&#10095;</span></dt>
                <dd> Durante la calibrazione, l'analizzatore esamina la stabilità (velocità del
                  cambiamento) del segnale dalla sonda e se questa varia di oltre il 10% durante il
                  conto alla rovescia, l'analizzatore impedisce la calibrazione per evitare che la
                  procedura di calibrazione introduca errori. 
                </dd>
              </dl>
