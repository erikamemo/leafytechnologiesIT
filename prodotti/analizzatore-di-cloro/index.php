                      alt="Il grafico illustra la risposta tipica del sensore di cloro HaloSense al variare del pH, maggiori info nel testo." />
                  </picture>
                  <figcaption style="text-align: center;"> Risposta tipica delle sonde alla variazione
                    del pH 
                  </figcaption>
                </figure>
                <p> Dal grafico è possibile osservare come, un sistema amperometrico tradizionale (linea
                  rossa), soffra di una perdita di segnale veloce ed accentuata quando il pH aumenta
                  oltre al pH 7.2. Un tampone chimico riduce il pH portandolo intorno a valori che
                  oscillano tra pH 5 e pH 6, assicurandosi così che la maggior parte del cloro residuo
                  sia presente nella forma di HOCl, forma che i <strong> sistemi tradizionali</strong>
                  riescono a misurare. Si comprende quindi la necessità di controllare il pH nei
                  sistemi tradizionali. 
                </p>
                <h3>La soluzione HaloSense</h3>
                <p> Il sensore di cloro libero HaloSense (linea blu sul grafico) misura sia l'HOCl ed
                  indirettamente, grazie allo speciale elettrolita che effettua la conversione
                  dell'dell'OCl<sup>-</sup> in HOCl, anche la maggior parte dell'OCl<sup>-</sup>
                  presente. Ciò si traduce in un <strong>effetto della variazione del pH sulla qualità
                  della misura notevolmente ridotto</strong> e significa che, nella maggior parte
                  delle applicazioni di monitoraggio del cloro, non vi è bisogno di alcuna soluzione
                  tampone e/o compensazione del pH. Questo porta a notevoli risparmi su reagenti,
                  manutenzione, sensoristica e tempo. 
                </p>
