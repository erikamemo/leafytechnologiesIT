              <p> Nella maggior parte dei sensori di pH, la <strong>giunzione</strong> risulta spesso essere la parte più problematica dove il rischio di errore è maggiore. Nei sensori pHSense la giunzione è costituita da un solido elettrochimicamente attivo e questo produce un sensore di pH più robusto. </p>
