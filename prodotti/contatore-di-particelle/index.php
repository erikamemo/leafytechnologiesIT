          </figure>
          <p> Il monitoraggio in continuo della torbidità all'uscita di un filtro è essenziale e persino un requisito legale in alcuni paesi. La <a href="<?php echo $root; ?>prodotti/turbidimetro-in-linea/">torbidità</a> è una buona misurazione per determinare<strong> la chiarezza dell'acqua</strong> , fornisce una lettura unica e comprensibile per gli operatori ed è più sensibile alle particelle più piccole (particelle di dimensioni &lt; 1 μm). </p>
          <p> I contatori di particelle invece si focalizzano su particelle più grandi (di solito più grandi di 2 μm) e forniscono ulteriori informazioni oltre a quelle fornite dai contatori di torbidità, in particolare informazioni riguardanti alla <strong>rottura di un filtro ed al passaggio di particelle</strong> di dimensioni dei batteri di Cryptosporidium, spesso non rilevabili da un turbidimetro tradizionale. </p>
          <p> In passato molte aziende municipalizzate, aziende per il trattamento dell’acqua, aziende pubbliche e private hanno installato misuratori di torbidità su filtri, ma non hanno installato contatori di particelle soprattutto <strong> a causa del costo più elevato</strong> dei contatori di particelle. </p>
          <h3>Problemi di coagulazione VS sfondamento del filtro</h3>
          <figure style="float: right; clear: left; margin-left: 16px;">
            <picture>
              <source type="image/webp" srcset="<?php echo $root; ?>images/pi-approfondimenti/grafico-sfondamento-filtro.webp">
              <img src="<?php echo $root; ?>images/pi-approfondimenti/grafico-sfondamento-filtro.jpg" alt="Sfondamento Filtro"/> </picture>
            <figcaption style="text-align: right;"> Sfondamento filtro </figcaption>
          </figure>
          <p> Storicamente nella maggior parte delle epidemie di Cryptosporidium vi è stato sia un <strong> fallimento della <a href="<?php echo $root; ?>prodotti/sistema-controllo-coagulazione/">coagulazione</a></strong> sia della filtrazione. Si vi è un fallimento della coagulazione, le particelle non coagulate possono passare direttamente attraverso il filtro ed, in questo caso, un misuratore di torbidità rileva questo fenomeno e lo mostra all'operatore, dando così la possibilità di intervenire. </p>
          <p> Se invece il filtro inizia a: </p>
          <ul>
            <li> far passare particelle di dimensioni grandi, ad esempio, il Cryptosporidium ha una dimensione in media di <strong>7 micrometri,</strong> </li>
            <li> mostrare segni di cedimento, </li>
            <li> <strong>canalizzarsi</strong>: un canale si apre dall'alto verso il basso e crea un percorso di facile attraversamento per l'acqua, allora </li>
          </ul>
          <p> è improbabile che questo venga rilevato da un misuratore di torbidità, ma può essere chiaramente visto con un contatore di particelle. </p>
          <h3>Costi</h3>
          <p> In passato, monitorare un banco di 8 filtri con un contatore di particelle su ogni filtro avrebbe comportato un costo di circa €45,000 il che era un grosso ostacolo. Oggi invece con il sistema CounterSense/FilterSense si offre <strong>la stessa funzionalità a circa metà prezzo</strong>. </p>
