          <p> <strong>I misuratori di torbidità sono più sensibili alle particelle di piccole dimensioni che sono presenti se la coagulazione fallisce. D’altra parte, i contatori di particelle sono più sensibili ai problemi correlati alla filtrazione.</strong> </p>
