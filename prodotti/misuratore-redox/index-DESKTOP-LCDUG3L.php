                <figcaption style="text-align: right;"> Sensore Redox Uno </figcaption>
              </figure>
              <p> La linea di misuratori ed <strong>analizzatori redox</strong>, ORPSense, utilizza alcuni tra i migliori sensori di <strong>potenziale di ossido-riduzione</strong> attualmente disponibili al mondo per la misura in continuo dell'<abbr title="Oxidation Reduction Potential" lang="en">ORP</abbr> in qualsiasi soluzione acquosa. Questi sensori a sfera di platino, e con elettrodi di riferimento integrati, non utilizzano reagenti, sono estremamente stabili ed hanno una manutenzione e costi di manutenzione ridotti. </p>
              <p> I misuratori redox in linea sono tra gli analizzatori più utilizzati negli impianti di trattamento delle acque. Tuttavia, nonostante la loro diffusione, questi sensori creano non pochi problemi agli operatori: molti sensori tradizionali hanno problemi a <strong>mantenere la calibrazione</strong> per lunghi periodi, difficoltà a funzionare in soluzioni difficili e richiedono molta manutenzione. Con la linea ORPSense si offre un misuratore di redox stabile e affidabile che offra anche la possibilità di <strong>ricalibrazione automatica</strong>. </p>
              <p> La flessibilità della linea ORPSense, unita alla conoscenza dei processi di trattamento ed all'esperienza maturata dai tecnici della <i lang="en">Leafy Technologies</i>, permettono di configurare l'analizzatore in modo tale da rispondere ai <strong> bisogni specifici</strong> di ogni impianto. </p>
              <p> La natura modulare del sistema ORPSense (sensore + analizzatore) permette di acquistare soltanto le opzioni richieste, senza perdere la capacità di integrare funzionalità aggiuntive qualora fossero necessarie in futuro. Se il progetto richiede un sistema semplice, conviene orientarsi verso <strong><a href="<?php echo $root; ?>prodotti/analizzatore-multiparametrico-base/">l'analizzatore multiparametrico di base CRONOS<sup>®</sup></a></strong>, altrimenti, se vi è la necessità di monitorare vari parametri (fino a 16), gestire vari segnali provenienti da strumenti diversi ed effettuare la telegestione via internet del sistema, si consiglia di orientarsi verso <strong><a href="<?php echo $root; ?>prodotti/analizzatore-multiparametrico-avanzato/">l'analizzatore multiparametrico avanzato CRIUS<sup>®</sup></a></strong>. </p>
              <p> Entrambi gli analizzatori possono essere interfacciati con vari sensori (<a href="<?php echo $root; ?>prodotti/analizzatore-di-ph/">pH</a>, <a href="<?php echo $root; ?>prodotti/analizzatore-di-conducibilita/">conducibilità</a> etc.), eliminando la necessità di acquistare uno strumento per ogni parametro. </p>
              <p> Il sistema ORPSense viene utilizzato in molte applicazioni come il monitoraggio in continuo del potenziale redox, il controllo del dosaggio ed altre. Entrambi gli analizzatori offrono uscite analogiche (4-20 mA), uscite relè e vari protocolli di comunicazione. </p>
              <p> Per maggiori informazioni contattarci ai seguenti recapiti: Tel: <a href="tel:+393714388849"> +39 371 438 8849</a>, E-mail: <a href="mailto:info@leafytechnologies.com">info@leafytechnologies.com</a>. </p>
              <h3>Principio di funzionamento</h3>
              <figure style="float: right; clear: left; margin-left: 16px;">
                <picture>
                  <source type="image/webp" srcset="<?php echo $root; ?>images/pi-prodotti/sensore-redox-numero-due.webp">
                  <img src="<?php echo $root; ?>images/pi-prodotti/sensore-redox-numero-due.jpg" alt="Sensore di redox" />
                </picture>
                <figcaption style="text-align: right;"> Sensore di redox, modello n. 2 </figcaption>
              </figure>
              <p> La tecnologia alla base dei sensori redox è un <strong> elettrodo di vetro</strong> riempito con un polimero brevettato. Questi elettrodi redox puntano ad un mercato medio-alto rispetto agli elettrodi di processo presenti comunemente sul mercato ed è progettato per avere lunga durata, bassa deriva ed alta affidabilità. </p>
              <p> A differenza di molti sensori redox, non è necessario effettuare una lunga opera di selezione per identificare un elettrodo specifico (spesso dovendo cercare tra decine di elettrodi differenti) per trovare quello adatto al proprio processo: l'elettrodo ORPSense funziona in tutti gli ambienti con un pH compreso tra <strong>pH 2 a pH 12</strong> ed una conduttività da <strong> 0 μS in su</strong>. </p>
              <p> Grazie alla loro durata ed ai requisiti di manutenzione notevolmente ridotti, questi sensori di ORP risultano abbastanza economici nel medio-lungo periodo: in genere richiedono solo una calibrazione ogni due o tre mesi. Per maggiori informazioni visualizzare <a href="<?php echo $root; ?>docs/schede-prodotto/analizzatore-redox.pdf">la brochure</a>. </p>
              <figure style="float: right; clear: left; margin-left: 16px;">
                <picture>
                  <source type="image/webp" srcset="<?php echo $root; ?>images/pi-prodotti/sensore-redox-numero-tre.webp">
                  <img src="<?php echo $root; ?>images/pi-prodotti/sensore-redox-numero-tre.jpg" alt="Sensore di redox" />
                </picture>
                <figcaption style="text-align: right;"> Sensore di redox, modello n. 3 </figcaption>
              </figure>
              <p> Il misuratore di redox offerto dalla <i lang="en">Leafy Technologies</i> elimina il requisito della calibrazione continua da parte dell'operatore poiché utilizza un elettrodo estremamente stabile. L'ORPSense, infatti, calcola un <strong>grado di affidabilità</strong> della propria calibrazione e informa l'utente se questo valore è basso o alto. Se il sistema rileva un guasto nel sensore redox, fornisce informazioni sulla possibile origine dell'errore, con un sistema di diagnostica automatica. </p>
              <p> L'analizzatore è munito di <strong>controllo <abbr title="Proporzionale-Integrale-Derivativo">PID</abbr></strong> ed è dotato di opzioni di controllo di processo, registro dati, uscite analogiche e relè, comunicazioni seriali (tra le quali <strong>TCP, Ethernet, Modbus, Profibus </strong>). L'opzione di telegestione è disponibile via internet (sia attraverso una scheda SIM, sia attraverso LAN/Ethernet). Per concludere il sistema ORPSense offre tutte le opzioni che si possono desiderare in un misuratore redox pur mantenendo un costo relativamente basso ed un ottimo rapporto qualità-prezzo. </p>

