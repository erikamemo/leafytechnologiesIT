                <h3>Analizzatori per Acqua 4.0 ed Industria 4.0</h3>
                <p> Le espressioni <strong>Acqua 4.0</strong>, <strong>Industria 4.0</strong>, <strong>Internet delle Cose</strong> e <strong>SMART e DIGITAL</strong> sono oramai utilizzate spesso nel settore dell'acqua, ma cosa significano e dove si inseriscono gli strumenti della <span lang="en">Pi </span> in questo contesto? </p>
                <p> <strong>Industria 4.0 </strong> si riferisce al concetto che si sta vivendo una quarta rivoluzione industriale in cui le fabbriche saranno sempre più intelligenti. Vi sono 4 principi fondanti: </p>
                <ul>
                  <li> <strong>interoperabilità,</strong> </li>
                  <li> <strong>trasparenza delle informazioni</strong>, </li>
                  <li> <strong>assistenza tecnica</strong>, </li>
                  <li> <strong>decentramento</strong>. </li>
                </ul>
                <p> <strong>Acqua 4.0</strong> è il modo in cui questa tecnologia e questa filosofia saranno implementate ed influenzeranno il settore idrico. </p>
                <p> <strong>L'Internet delle cose</strong> si riferisce a tutti i dispositivi che si usano continuamente ed alla capacità che avranno di comunicare tra di loro e prendere decisioni indipendenti. </p>
                <p> È generalmente accettato che <strong>SMART</strong> si riferisca alla crescente sofisticazione delle apparecchiature che hanno sempre più mezzi per prendere decisioni decentralizzate, quindi, è questa tecnologia che consente l'implementazione dell’Industria 4.0 o dell’Acqua 4.0. <strong>DIGITAL</strong> si riferisce invece alla capacità di comunicazione di queste tecnologie. Ad esempio, nel 1990 un analizzatore di cloro misurava il cloro in acqua ed emetteva un'uscita di 4-20 mA proporzionale al cloro. </p>
                <p> Nel 2018 il CRIUS<sup>®</sup> HaloSense (sviluppato tenendo in mente i principi dell’Acqua 4.0) può fornire l'uscita 4-20 mA ed i relè, ma ha anche altre opzioni di comunicazione digitale (interoperabilità), ha spazio per un massimo di 16 sensori (trasparenza delle informazioni), dispone di connessione internet wireless e cablata con <a href="<?php echo $root; ?>prodotti/accesso-remoto/">accesso remoto</a> (assistenza tecnica) e piena capacità PID con valori obiettivo gestiti da remoto: un altro dispositivo può impostare il valore obiettivo (<i lang="en">setpoint</i>). </p>
