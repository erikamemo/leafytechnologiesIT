              <h2>Applicazioni</h2>
              <p> Il CRIUS<sup>®</sup> è attualmente utilizzato in una moltitudine di applicazioni di <strong>trattamento delle acque</strong>: impianti di potabilizzazione, di depurazione e di trattamento delle acque industriali. </p>
              <p> Il CRIUS<sup>®</sup> è utile anche per i professionisti del mercato delle piscine dove, le opzioni di controllo avanzate e la capacità dei sensori, garantiscono una migliore qualità delle acque di balneazione. Anche in questo caso l'accesso remoto si sta rivelando essenziale per i  clienti insieme al <strong>controllo <dfn><abbr lang="en">VSD</abbr></dfn> </strong>(<i lang="en">Variable Speed Drive</i> ossia l'azionamento a velocità variabile) che riduce in modo massiccio l'uso di elettricità e le emissioni di CO<sub>2</sub>. </p>
              <p> Nel mercato municipale dell'acqua potabile, le opzioni di controllo avanzate rendono il CRIUS<sup>®</sup> l'analizzatore di scelta per aree come il <a href="<?php echo $root; ?>prodotti/sistema-controllo-coagulazione/">controllo della coagulazione</a> oppure il controllo di sensori avanzati di <a href="<?php echo $root; ?>prodotti/analizzatore-di-cloro/">cloro</a>, di <a href="<?php echo $root; ?>prodotti/analizzatore-di-ossigeno-disciolto/">ossigeno disciolto</a> e il sistema per la <a href="<?php echo $root; ?>prodotti/analizzatore-corrente-fluente-coagulazione/">misura della corrente fluente</a>, ossia un parametro correlato al potenziale zeta e correlato alla domanda di coagulante dell'acqua. </p>
