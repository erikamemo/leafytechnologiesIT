              <p> L’<strong>analizzatore multiparametrico avanzato</strong> CRIUS<sup>®</sup> può gestire più sensori (<strong>fino a 16</strong>), offre  funzionalità avanzate e tanta flessibilità. Con uno schermo a colori integrato ed un modem 3G/4G opzionale, il CRIUS<sup>®</sup> supporta protocolli di comunicazione come Profibus, Modbus ASCII, Modbus RTU, Modbus TCP ed un <a href="<?php echo $root; ?>prodotti/accesso-remoto/">sistema avanzato di telegestione e telecontrollo via internet</a>. </p>
