              <p> Il sistema viene di solito acquistato da aziende specializzate nella costruzione e/o nella gestione di piscine. In questi casi è possibile offrire <strong>l’opzione di personalizzare lo strumento</strong> con il proprio marchio (OEM), sia sullo strumento (l’adesivo), sia nel software, sia nel portale di accesso remoto. In questo modo l’azienda partner può continuare a promuovere il proprio marchio sfruttando la  tecnologia AquaSense. </p>
              <p> Inoltre, il sistema di <strong>azionamento a velocità variabile</strong> permette di controllare la velocità delle pompe di ricircolo dell’acqua delle piscine, offrendo così la possibilità di risparmiare enormemente sui costi dell’elettricità e, in molti casi, ripagare lo strumento in meno di 12 mesi. </p>
              <p> Quando si parla di <strong>centraline per il controllo del cloro e del pH</strong>, che verranno installate in impianti termali, bisogna essere consapevoli che vi sono ulteriori sfide: le alte temperature ed il volume d'acqua ridotto. </p>
              <p> Queste due componenti significano che in questi impianti è assolutamente necessario utilizzare sensori di alta qualità, che siano in grado di <strong>rispondere velocemente</strong> alle variazioni delle caratteristiche dell’acqua e riescano ad operare ad alte temperature per periodi prolungati. </p>
