                <li> <strong>Miglior controllo</strong>: attraverso un controllo più preciso del processo si riesce a ridurre l’utilizzo di sostanze chimiche, ridurre i <strong>costi di gestione</strong> dell’impianto e migliorare l'esperienza di balneazione dei propri clienti </li>
