                <h3>Ritorno sull'investimento dai 3 ai 12 mesi</h3>
                <p> Per molti anni, nel settore delle piscine e degli impianti termali, lo standard per la misurazione del cloro e del pH è stato estremamente basso. Grazie all’utilizzo di sensori di alta gamma il sistema AquaSense permette di: </p>
                <figure style="float: right; clear: left; margin-left: 16px;">
                  <picture>
                    <source type="image/webp" srcset="<?php echo $root; ?>images/pi-approfondimenti/controllo-invertitore-grafico.webp">
                    <img src="<?php echo $root; ?>images/pi-approfondimenti/controllo-invertitore-grafico.jpg" alt="Controllo Invertitore"/> 
                  </picture>
                  <figcaption style="text-align: right;"> Controllo dell'<i lang="en">inverter</i> </figcaption>
                </figure>
                <ul>
                  <li> risparmiare denaro <strong>controllando le pompe di ricircolo</strong> dell’acqua della piscina; </li>
                  <li> eliminare e controllare la crescita batteriologica nelle piscine e nelle stazioni termali; </li>
                  <li> ottimizzare l'uso di sostanze chimiche; </li>
                  <li> eliminare il fenomeno degli <strong>"occhi rossi"</strong> e l’odore di cloro. </li>
                </ul>
                <p> La centralina per piscina AquaSense è un dispositivo sofisticato che controlla il livello di <strong>cloro, di pH</strong> e, se vi è un attuatore a velocità variabile, la velocità delle pompe di ricircolo. </p>
                <p> La maggior parte delle pompe di ricircolo delle piscine sono sovra-specificate rispetto alle necessità reali e pertanto, con la giusta strumentazione, è possibile ridurre/eliminare gli sprechi. Ogni volta che si <strong>risparmia elettricità</strong> si riducono anche le emissioni di CO<sub>2</sub> e l'impronta di carbonio della piscina e dell’impianto termale. Sempre più spesso per molte organizzazioni la riduzione delle emissioni di CO<sub>2</sub> è un fattore significativo nella scelta delle attrezzature da acquistare e, in alcuni casi, può portare anche a sussidi statali. </p>
                <p> Per poter controllare la pompa di ricircolo dell’acqua della piscina, il motore della pompa deve essere dotato di un <strong>invertitore</strong>. Di seguito si descrive la logica di controllo dell'<i lang="en">inverter</i>. </p>
                <figure style="float: right; clear: left; margin-left: 16px;">
                  <picture>
                    <source type="image/webp" srcset="<?php echo $root; ?>images/pi-approfondimenti/grafico-comparazione-pid-soglie.webp">
