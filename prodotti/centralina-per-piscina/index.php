                <p> Infatti, maggiore sarà il numero di persone presenti nella piscina, maggiore sarà la domanda di cloro, maggiore la necessità di far <strong>ricircolare l’acqua</strong> della piscina per aumentare la filtrazione. </p>
                <p> Questa capacità di controllare la pompa di ricircolo dell’acqua in base alla domanda di cloro significa che gli operatori possono risparmiare ingenti somme di denaro sull'elettricità durante il giorno (ossia quando l'elettricità costa di più). In <strong>piscine di grosse dimensioni</strong> e mal gestite questo può portare ad un ritorno sull'investimento tra i 3 ed i 6 mesi. </p>
