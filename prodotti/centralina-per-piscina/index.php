              <p> Nella maggior parte delle <strong>piscine e degli impianti termali</strong> il cloro è usato come disinfettante, quindi, un analizzatore di cloro in linea che misuri la quantità di disinfettante da dosare è, non solo auspicabile, ma  necessario. </p>
