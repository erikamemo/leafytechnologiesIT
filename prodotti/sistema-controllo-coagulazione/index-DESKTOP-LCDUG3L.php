              <p> Il sistema CoagSense per il controllo e l'ottimizzazione della coagulazione in continuo, utilizza una serie di sensori, <strong>compresi sensori già installati sull'impianto</strong>, connessi ad un analizzatore centrale. L'analizzatore, quindi, elabora i vari segnali, li manipola e produce un segnale in uscita che controlla il <strong>dosaggio di un coagulante</strong>. I segnali/sensori solitamente utilizzati sono descritti di seguito. </p>
              <p> <strong>Portata</strong>: usata per aumentare o diminuire la dose di coagulante in maniera proporzione alla portata stessa. </p>
              <p> <strong>Il pH dell'acqua all'entrata impianto</strong>: questo è forse il parametro più importante che influenza la coagulazione. Il sistema CoagSense utilizza un sensore di pH a giunzione polimerica che dà una <strong>lettura estremamente rapida</strong> ed affidabile ed invia un allarme/segnale se il pH si muove al di fuori di un intervallo predeterminato. Questo sensore si dovrebbe preferibilmente usare per controllare il pH dell'acqua non depurata attraverso di un <strong>controllo PID</strong> separato a ciclo continuo. </p>
              <figure style="float: right; clear: left; margin-left: 16px;">
                <picture>
                  <source type="image/webp" srcset="<?php echo $root; ?>images/pi-prodotti/analizzatore-uv254.webp">
