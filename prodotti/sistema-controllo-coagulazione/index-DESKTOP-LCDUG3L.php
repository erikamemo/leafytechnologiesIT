              <h2>Introduzione</h2>
              <p> Il sistema per il controllo e l'ottimizzazione della coagulazione CoagSense ideato dalla <i lang="en">Process Instruments</i>, consiste di un <strong>analizzatore integrato multiparametrico</strong> e modulare che può essere adattato a diversi impianti e situazioni, con l'aggiunta di vari sensori come ad esempio quello per il <a href="<?php echo $root; ?>prodotti/analizzatore-di-ph/">pH</a>, il flusso, la temperatura, <a href="<?php echo $root; ?>prodotti/analizzatore-uv254/">l'UV254</a>, la <a href="<?php echo $root; ?>prodotti/turbidimetro-in-linea/">torbidità</a> ed il <a href="<?php echo $root; ?>prodotti/analizzatore-corrente-fluente-coagulazione/">misuratore di corrente fluente</a> (<i lang="en">streaming current)</i>. </p>
              <figure style="display: block; text-align: center;">
                <picture>
                  <source type="image/webp" srcset="<?php echo $root; ?>images/pi-prodotti/sistema-controllo-coagulazione.webp">
                  <img src="<?php echo $root; ?>images/pi-prodotti/sistema-controllo-coagulazione.jpg" alt="Sistema di controllo della coagulazione" />
                </picture>
                <figcaption style="text-align: center;"> Sistema di controllo della coagulazione </figcaption>
              </figure>
              <p> Partendo dalla misura di questi parametri, il sistema invia un segnale di controllo della coagulazione proporzionale al flusso che può essere poi gestito da un sistema <abbr lang="en" title="Supervisory Control and Data Acquisition ">SCADA</abbr> o può essere utilizzato direttamente per controllare una <strong>pompa dosatrice del coagulante</strong>. </p>
              <p> La coagulazione è influenzata da un gran numero di variabili, alcune sono associate a tutti i tipi di processo di trattamento delle acque mentre altre sono specifiche al processo/impianto preso in considerazione. Tra queste variabili vi sono: </p>
              <ul>
                <li> il pH dell'acqua all'entrata, </li>
                <li> l'alcalinità, </li>
                <li> la torbidità, </li>
                <li> la carica organica, </li>
                <li> la temperatura, </li>
                <li> il coagulante usato, </li>
                <li> il pH dopo l'aggiunta del coagulante ed infine </li>
                <li> aspetti fisici incluso il punto di dosaggio e la miscelazione. </li>
              </ul>
              <p> Questi parametri variano molto da impianto ad impianto, da fonte d'acqua a fonte d'acqua e persino da giorno a giorno. </p>
              <p> Questo significa che <strong>non vi è una soluzione unica</strong>. Un metodo di controllo della coagulazione non può essere sperimentato in un impianto e replicato in maniera identica in un altro. Ogni sistema deve essere progettato e specifico per ogni impianto di trattamento delle acque. </p>
              <p> Ovviamente, prima di modificare sistemi di controllo della coagulazione esistenti, può essere utile installare e monitorare l'efficienza del sistema esistente attraverso ad un <strong>analizzatore di coagulazione</strong>. </p>
