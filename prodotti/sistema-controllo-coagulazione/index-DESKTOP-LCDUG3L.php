              <p> <strong>Il pH dell'acqua del coagulante</strong>: usato per inviare un allarme/notifica se il pH non rientra nell'intervallo predeterminato per un controllo ottimale e gestire azioni correttive. </p>
              <p> <strong>L'UV254</strong>: durante i periodi di bassa torbidità ed elevata assorbanza UV specifica (<abbr title="Specific Ultra-Violet Absorbance" lang="en">SUVA</abbr><sup><a href="#riferimento">1</a></sup>), la rimozione di sostanze organiche e quindi, di conseguenza, la riduzione dei <abbr lang="en" title="Trihalomethanes">THM</abbr> (Trialometani) può essere ottenuta in maniera ottimale utilizzando azioni di controllo in avanti (<i lang="en">feed forward</i>) attraverso la lettura di un segnale UVA. </p>
              <p> <strong>Torbidità delle acque grezze in entrata</strong>: quando la torbidità è elevata, i sensori di UVA iniziano a rispondere alla torbidità e non misurano più efficacemente la componente organica. Durante questi periodi è spesso preferibile utilizzare un altro metodo per effettuare il controllo e questo può potenzialmente essere la misura della stessa torbidità. </p>
              <figure style="float: right; clear: left; margin-left: 16px;">
                <picture>
