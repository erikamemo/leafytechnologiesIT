            <article id="Applicazioni">
              <h2>Applicazioni</h2>
              <p> Le versioni precedenti di sistemi multiparametrici per il controllo della coagulazione erano basate su sistemi PLC che per loro natura sono complessi, estremamente costosi e raramente definibili come robusti o affidabili. </p>
              <figure style="display: block; text-align: center;">
                <picture>
                  <source type="image/webp" srcset="<?php echo $root; ?>images/pi-sito/piccolo-impianto-trattamento-acque.webp">
                  <img src="<?php echo $root; ?>images/pi-sito/piccolo-impianto-trattamento-acque.jpg" alt="Impianto trattamento acque" />
                </picture>
                <figcaption style="text-align: center;"> Impianto di trattamento delle acque </figcaption>
              </figure>
              <p> L'analizzatore per la coagulazione CoagSense è un <strong>sistema unico</strong> che non ha bisogno di sistemi PLC o simili ed è completamente configurabile. In questo modo è possibile gestire le variazioni tra un impianto e l'altro o variazioni all'interno dello stesso impianto. </p>
              <p> Per molti anni, i gestori degli impianti hanno cercato di controllare la <strong>dose di coagulante </strong>utilizzando un unico parametro. Questo parametro è stato di volta in volta: <a href="<?php echo $root; ?>prodotti/analizzatore-di-ph/">il pH</a>, <a href="<?php echo $root; ?>prodotti/turbidimetro-in-linea/">la torbidità</a>, <a href="<?php echo $root; ?>prodotti/analizzatore-corrente-fluente-coagulazione/">il flusso di carica elettrica superficiale</a> o l'assorbanza / trasmittanza della luce UV a 254 nm (<a href="<?php echo $root; ?>prodotti/analizzatore-uv254/">UV254</a>). Tutti questi parametri sono stati usati per controllare la coagulazione su vari impianti di trattamento delle acque con vari gradi di successo. </p>
              <p> Il <strong>sistema di controllo della coagulazione</strong> della Pi consente il controllo della coagulazione su di un impianto di trattamento delle acque utilizzando uno O più O tutti questi parametri allo stesso tempo. </p>
              <h3>Controllo automatico della coagulazione in impianti senza operatori</h3>
              <p> Molti impianti di trattamento delle acque utilizzano ancora il controllo manuale per il dosaggio del coagulante (solitamente sali d'alluminio o ferrico). Finché la <strong>qualità dell'acqua grezza</strong> è stabile, questo è un metodo efficace per gestire il dosaggio del coagulante, tuttavia, quando la qualità dell'acqua grezza cambia, il controllo manuale del coagulante non funziona più, in particolare quando gli impianti non sono controllati durante le ore notturne o nei fine settimana. </p>
              <figure style="display: block; text-align: center;">
                <picture>
                  <source type="image/webp" srcset="<?php echo $root; ?>images/pi-prodotti/misuratore-potenziale-di-flusso.webp">
                  <img src="<?php echo $root; ?>images/pi-prodotti/misuratore-potenziale-di-flusso.jpg" alt="Misuratore potenziale di flusso" />
                </picture>
                <figcaption style="text-align: center;"> Misuratore di corrente fluente </figcaption>
              </figure>
              <p> Quando ci si aspetta che l'acqua grezza in entrata avrà un contenuto organico elevato (ad esempio, quando si prevede pioggia) gli operatori aumentano il dosaggio di coagulante . <br />
                Questo può portare ad una scarsa coagulazione prima, durante e dopo la pioggia (se presente) e può anche introdurre <strong>problemi a lungo termine </strong>come il sovraccarico dei filtri, tempi di utilizzo dei filtri più brevi ed un <strong>aumento dei residui di alluminio</strong> (nel caso sia utilizzato come coagulante) . </p>
              <p> In molti impianti è possibile automatizzare il controllo del coagulante attraverso la misura della così detta <i lang="en">streaming current</i>, ossia la corrente fluente. Grazie a questa misura si può effettivamente aumentare e diminuire il dosaggio del coagulante in modo automatico in risposta alla variazione della qualità dell'acqua. Questo è particolarmente utile nelle aree con acqua pura ed a bassa alcalinità. </p>

