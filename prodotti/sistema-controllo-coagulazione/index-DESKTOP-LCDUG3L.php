              <h3>Sensoristica</h3>
              <figure style="float: left; clear: right; margin-right: 16px; ">
                <picture>
                  <source type="image/webp" srcset="<?php echo $root; ?>images/pi-prodotti/sensore-pH-uno.webp">
                  <img src="<?php echo $root; ?>images/pi-prodotti/sensore-pH-uno.jpg" alt="Sensore pH" />
