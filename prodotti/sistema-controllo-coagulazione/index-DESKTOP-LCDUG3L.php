              <p> <strong>Corrente fluente</strong> (<i lang="en">streaming current</i>): con le giuste condizioni dell'acqua e/o col giusto coagulante, il controllo col misuratore di corrente fluente può offrire il miglior strumento per il controllo della coagulazione. <br />
                Installato in oltre 5,000 impianti in tutto il mondo, i sistemi di corrente fluente possono offrire una soluzione robusta ed economica. Comprendere la relazione tra <strong>pH e corrente fluente</strong> è essenziale per l'implementazione efficace di questo sistema di coagulazione. </p>
              <p> Infatti, la corrente fluente permette di misurare tutte le variabili che influiscono sulla <strong> neutralizzazione della carica</strong>, un aspetto chiave della coagulazione. Se non utilizzato direttamente per il controllo, un misuratore di corrente fluente può essere estremamente utile come sistema di monitoraggio della coagulazione, monitorando continuamente le variazioni di qualsiasi aspetto della coagulazione, compresi i cambiamenti nella chimica dell'acqua, il coagulante e l'intervallo operativo della pompa di dosaggio. </p>
