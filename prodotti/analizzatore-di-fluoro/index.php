              <p> La linea di analizzatori e sensori per la misurazione del fluoro in continuo utilizza alcuni tra i migliori sensori esistenti al mondo per misurare il contenuto di <strong>fluoro in acqua potabile</strong>. </p>
              <p> Questi sensori sono elettrodi iono-selettivi (<abbr lang="en" title="Ion Selective Electrode">ISE</abbr>) con riferimento integrale, compensazione automatica della temperatura e giunzione polimerica solida. Questi sistemi non utilizzano reagenti, sono <strong>estremamente stabili</strong> ed hanno una manutenzione e costi di proprietà ridotti. </p>
              <p> La flessibilità della linea FluoriSense, unita alla conoscenza dei processi di trattamento ed all'esperienza maturata dai tecnici della <i lang="en">Leafy Technologies</i>, permettono di configurare l'analizzatore in modo tale da rispondere ai <strong> bisogni specifici</strong> di ogni impianto. </p>
              <p> La natura modulare del sistema FluoriSense (sensore + analizzatore) permette di acquistare soltanto le opzioni richieste, senza perdere la capacità di integrare funzionalità aggiuntive qualora fossero necessarie in futuro. Se il progetto richiede un sistema semplice, conviene orientarsi verso <strong><a href="<?php echo $root;?>prodotti/analizzatore-multiparametrico-base/">l'analizzatore multiparametrico di base CRONOS<sup>®</sup></a></strong>, altrimenti, se vi è la necessità di monitorare vari parametri (fino a 16), gestire vari segnali provenienti da strumenti diversi ed effettuare la telegestione via internet del sistema, si consiglia di orientarsi verso <strong><a href="<?php echo $root;?>prodotti/analizzatore-multiparametrico-avanzato/">l'analizzatore multiparametrico avanzato CRIUS<sup>®</sup></a></strong>. </p>
              <p> Entrambi gli analizzatori possono essere interfacciati con vari sensori (<a href="<?php echo $root;?>prodotti/analizzatore-di-ph/">pH</a>, <a href="<?php echo $root;?>prodotti/misuratore-redox/">redox</a>, <a href="<?php echo $root;?>prodotti/analizzatore-di-cloro/">cloro</a> etc.), eliminando la necessità di acquistare uno strumento per ogni parametro. </p>
              <p> Per maggiori informazioni contattarci ai seguenti recapiti: Tel: <a href="tel:+393714388849"> +39 371 438 8849</a>, E-mail: <a href="mailto:info@leafytechnologies.com">info@leafytechnologies.com</a>. </p>
              <section>
                <h3>Principio di funzionamento </h3>
                <p> Quasi tutti gli analizzatori di fluoro utilizzano <strong>elettrodi iono-selettivi</strong> di fluoro. In alcuni sistemi si aumenta in livello di accuratezza dosando in continuo uno stabilizzatore ionico nel campione, generalmente noto come <dfn><abbr lang="en" title="Total Ionic Strength Adjustment Buffer">TISAB</abbr>: tampone di aggiustamento della forza ionica totale</dfn>. </p>
                <figure style="display: block; text-align: center;">
                  <picture>
                    <source type="image/webp" srcset="<?php echo $root; ?>images/pi-prodotti/sensore-fluoro-cella-flusso-doppia.webp">
                    <img src="<?php echo $root; ?>images/pi-prodotti/sensore-fluoro-cella-flusso-doppia.jpg" alt="Sensore di fluoro in cella di flusso"/>
                  </picture>
                  <figcaption style="text-align: center;"> Cella di flusso </figcaption>
                </figure>
                <p> Questi tamponi aiutano a "fissare" la forza ionica di una soluzione ma, nel far ciò, introducono un costo ed una complessità che sono considerati da molti non necessari nel controllo del <strong>dosaggio del fluoro</strong> in acqua potabile. </p>
                <p> L'analizzatore di fluoro FluoriSense non utilizza il metodo TISAB e, pertanto, è adatto per l'utilizzo in applicazioni in cui la forza ionica dell'acqua è relativamente stabile e non si ha bisogno di un livello di accuratezza elevatissimo. </p>
                <p> In molte applicazioni l'analizzatore di fluoro viene impiegato principalmente per garantire che il dosaggio del fluoro (proporzionale al flusso) stia funzionando correttamente e, in queste applicazioni, FluoriSense è il sistema ideale che fornisce una soluzione affidabile a <strong>costi contenuti</strong>, facile da installare e monitorare. </p>
                <p> Considerando le funzionalità aggiuntive che questo analizzatore di fluoro supporta, risulta essere una soluzione ottimale rispetto a quelle tradizionali. </p>
              </section>
