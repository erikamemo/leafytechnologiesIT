        <h2>FAQ</h2>
        <dl class="accordion">
        <dt>Cos'è il biofilm?<span aria-hidden="true">&#10095;</span></dt>
        <dd> Un biofilm è una matrice su superfici naturali o artificiali composta da un gruppo
        di microrganismi. Questi microrganismi sono spesso incorporati all'interno di una
        matrice di <strong>sostanze polimeriche extracellulari</strong> autoprodotte (<abbr
          lang="en" title="Extracellular polymeric substance">EPS) o all'interno di una
        melma. <br />
        Le cellule che crescono all'interno di un biofilm sono diverse dalle cellule
        della stessa specie nell'acqua, che sono spesso planctoniche o libere.</dd>
        <dt> Perché vi è la formazione dei biofilm? <span aria-hidden="true">&#10095;</span>
        </dt>
        <dd> Le cellule all'interno di una specie che costituisce un biofilm subiscono uno
        <strong>spostamento fenotipico</strong> in cui ampie parti dei geni sono regolate in
        modo differente. In parole semplici, il comportamento delle cellule cambia quando si
        trovano in un biofilm rispetto a quando sono in uno stato planctonico. <br />
        Formare un biofilm può dare alle cellule un vantaggio rispetto alla propria
        esistenza planctonica in quanto possono alterare l'ambiente (ad esempio il pH, la
        composizione chimica dell'acqua) e possono essere protette dall'azione dei biocidi
        grazie ad <strong>una "melma"</strong>. <br />
        La formazione del biofilm può essere innescata da varie cause, ma spesso si forma
        quando i microorganismi si trovano in un ambiente <strong>difficile</strong> ossia
        in un ambiente in cui sono sotto attacco o dove le sostanze nutritive e altre
        risorse sono scarse.
        </dd>
        <dt> Che dati fornisce il sensore di biofilm? <span aria-hidden="true">&#10095;</span>
        </dt>
        <dd> L'analizzatore di biofilm misura la corrente generata dal biofilm che cresce sulla
        superficie del sensore, superficie che rappresenta un <strong>ambiente
        preferenziale</strong> per la crescita dello stesso. In pratica, il sensore
        offre un "punto di atterraggio" per le cellule aderenti ed un ambiente
        <strong>elettricamente stimolante</strong> per fornire al biofilm le migliori
        possibilità di sopravvivenza. <br />
        Questo significa che, <strong>se vi è la possibilità di sviluppo di biofilm
        all'interno del sistema</strong>, allora questo crescerà in primo luogo sul
        sensore. Ovviamente si potrebbe argomentare che il sensore stimoli o agevoli la
        crescita del biofilm, ma questo non è il caso: il BioSense indica quando le
        condizioni nel sistema stanno cambiando in maniera tale da poter supportare la
        crescita del biofilm. <br />
        Questo <strong>cambiamento</strong> può essere il passaggio da un sistema senza
        crescita di biofilm ad un sistema che supporta la crescita di alcuni biofilm
        (situazione tipica all'interno dell'impianto idrico di un ospedale) o può essere il
        passaggio dalla crescita sporadica di biofilm ad una “esplosione” della crescita di
        biofilm (situazione possibile all'interno di una torre di raffreddamento).
        </dd>
        <dt> Che dati non può fornire il BioSense? <span aria-hidden="true">&#10095;</span></dt>
        <dd> Non esiste alcuna correlazione tra i conteggi planctonici e le letture
        dell'analizzatore di biofilm BioSense. <br />
        Non vi è alcuna correlazione tra lo spessore del biofilm ed il BioSense. Il BioSense
        misura <strong>l'attività elettrica biologica</strong> del biofilm, non il suo
        spessore. Il sensore di biofilm, BioSense, non può differenziare tra le varie
        specie. </dd>
        <dt> È possibile testare il BioSense in laboratorio? <span
          aria-hidden="true">&#10095;</span></dt>
        <dd> Generalmente è estremamente complicato testare il sensore in laboratorio. Un
        biofilm tende a formarsi in <strong>condizioni di scarsità</strong> quando un
        biofilm conferisce un vantaggio alla sopravvivenza dei batteri. In un laboratorio, i
        biofilm tendono a non essere gli stessi presenti nelle applicazioni reali in quanto
        sono monoculturali, sono indotti e, in genere, ricevono qualche forma di nutrimento.
        I biofilm di laboratorio sono normalmente <strong>non rappresentativi</strong> del
        biofilm presente in una applicazione reale, quindi, il BioSense, quando testato in
        laboratorio, non dà risultati positivi. Il miglior test è quello che si può
        effettuare in una applicazione reale. </dd>
        <dt> Che informazioni dà il BioSense sulla legionella? <span
          aria-hidden="true">&#10095;</span></dt>
        <dd> La legionella è causata dall'incubazione dei batteri della legionella che spesso
        crescono all'interno biofilm, dove sono protetti dai disinfettanti. Il
        <strong>sistema di monitoraggio del biofilm </strong>, BioSense, può dire se vi è
        rischio di crescita del biofilm nel proprio impianto idrico ed è un ottimo strumento
        nella lotta della legionella.
        </dd>
