                <p> Il sensore di acido peracetico <strong>amperometrico a membrana</strong> è un
                  sensore a due elettrodi che funziona ad un elevato potenziale il quale elimina la
                  deriva dello zero. Grazie ad un design unico non sono necessari reagenti e la
                  calibrazione risulta essere una semplice operazione a punto unico: <strong> non
                  serve calibrare lo zero</strong>. 
