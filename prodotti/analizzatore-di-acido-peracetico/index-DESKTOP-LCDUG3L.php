                senza manutenzione</strong>. 
              </p>
              <p> L'autopulizia è particolarmente utile nei processi di preparazione industriale degli
                alimenti ed in applicazioni in cui è probabile un accumulo di solidi nel campione.
                Visitare <a href="<?php echo $root; ?>prodotti/sistema-di-autopulizia/">la pagina del
                sistema AutoFlush per maggiori informazioni</a>. <br /><br />
                A destra un video con una dimostrazione del sistema AutoFlush. 
              </p>
