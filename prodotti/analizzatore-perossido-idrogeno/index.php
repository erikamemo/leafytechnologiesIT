              <h2>Introduzione</h2>
              <figure style="float: right; clear: left; margin-left: 16px;">
                <picture>
                  <source type="image/webp" srcset="<?php echo $root ?>images/pi-prodotti/sensore-perossido-idrogeno.webp">
