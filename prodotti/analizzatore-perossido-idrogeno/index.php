              </figure>
              <p> Gli analizzatori di <strong>perossido di idrogeno</strong> in linea (anche noti come misuratori di perossido di idrogeno) PeroxiSense utilizzano alcuni tra i migliori sensori di perossido di idrogeno al mondo. Si tratta di un <strong>sistema a membrana</strong> che non utilizza reagenti, è estremamente stabile ed ha costi di manutenzione e di proprietà ridotti. </p>
              <p> I sensori di <strong>perossido di idrogeno</strong> forniscono una misura in ppm o mg/l, sono accompagnati da varie celle di flusso (chiusa, aperta, singola, doppia) e sono interfacciabili con l’<a href="<?php echo $root; ?>prodotti/analizzatore-multiparametrico-base/">analizzatore base</a> o l’<a href="<?php echo $root; ?>prodotti/analizzatore-multiparametrico-avanzato/">analizzatore multiparametrico avanzato</a> i quali si differenziano per le diverse opzioni di comunicazione, visualizzazione e controllo. </p>
              <p> La flessibilità della linea PeroxiSense, unita alla conoscenza dei processi di trattamento ed all'esperienza maturata dai tecnici della <i lang="en">Leafy Technologies</i>, permettono di configurare l'analizzatore in modo tale da rispondere ai <strong> bisogni specifici</strong> di ogni impianto dove si debba misurare il perossido di idrogeno. </p>
              <p> La natura modulare del sistema PeroxiSense (sensore + analizzatore) permette di acquistare soltanto le opzioni richieste, senza perdere la capacità di integrare funzionalità aggiuntive qualora fossero necessarie in futuro. Se il progetto richiede un sistema semplice, conviene orientarsi verso <strong><a href="<?php echo $root; ?>prodotti/analizzatore-multiparametrico-base/">l'analizzatore multiparametrico di base CRONOS<sup>®</sup></a></strong>, altrimenti, se vi è la necessità di monitorare vari parametri (fino a 16), gestire vari segnali provenienti da strumenti diversi ed effettuare la telegestione via internet del sistema, si consiglia di orientarsi verso <strong><a href="<?php echo $root; ?>prodotti/analizzatore-multiparametrico-avanzato/">l'analizzatore multiparametrico avanzato CRIUS<sup>®</sup></a></strong>. </p>
              <p> Entrambi gli analizzatori possono essere interfacciati con vari sensori (<a href="<?php echo $root; ?>prodotti/analizzatore-di-ph/">pH</a>, <a href="<?php echo $root; ?>prodotti/misuratore-redox/">redox</a>, <a href="<?php echo $root; ?>prodotti/analizzatore-di-conducibilita/">conducibilità</a> etc.), eliminando la necessità di acquistare uno strumento per ogni parametro. </p>
              <p> L’analizzatore di perossido di idrogeno può essere utilizzato in molte applicazioni: da quelle che richiedono la semplice misurazione quelle applicazioni dove si vuole effettuare <strong>misurazione e controllo del dosaggio</strong> (ad esempio attraverso un controllo <abbr title="Proporzionale-Integrale-Derivativo">PID</abbr> o con soglie di una pompa dosificatrice). </p>
              <p> Per maggiori informazioni contattarci ai seguenti recapiti: Tel: <a href="tel:+393714388849"> +39 371 438 8849</a>, E-mail: <a href="mailto:info@leafytechnologies.com">info@leafytechnologies.com</a>. </p>
              <h3>Principio di funzionamento</h3>
              <p> Il sensore di perossido di idrogeno amperometrico è un sensore a due elettrodi che funziona ad un elevato potenziale applicato. Questo <strong>elimina la deriva zero</strong>. Il suo design unico significa che non sono necessari né reagenti né soluzioni tampone e la calibrazione è <strong>un'operazione semplice, a punto unico e senza lo zero</strong>. </p>
              <h4>Costo di proprietà</h4>
              <p> L'analizzatore di perossido di idrogeno è progettato per avere manutenzione ridotta, non aver bisogno di frequenti calibrazioni e <strong>ridurre il numero ed il costo delle parti di ricambio</strong>. Gli analizzatori PeroxiSense sono tra i migliori analizzatori di H<sub>2</sub>O<sub>2</sub> presenti sul mercato. </p>
              <p> Il sistema è in grado di controllare il <strong>dosaggio dell'H<sub>2</sub>O<sub>2</sub></strong> regolando automaticamente la portata, la velocità della pompa o le posizioni della valvola per mantenere il livello di perossido al valore obiettivo. Il <strong>dosaggio automatico</strong> può ridurre significativamente i costi associati ai reagenti ed aumentare il livello di controllo. </p>
