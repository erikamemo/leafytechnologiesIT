            <article id="Introduzione">
              <h2>Introduzione</h2>
              <p> Questo strumento è utilizzato per determinare la dose ottimale di coagulante da immettere in un impianto di trattamento delle acque.  Il LabSense, sistema da laboratorio, permette di misurare in maniera accurata la carica colloidale e ionica usando il principio della <strong> corrente fluente</strong> (carica elettrocinetica), concetto paragonabile al potenziale zeta. La domanda di carica viene determinata con titolazione al punto isoelettrico utilizzando il coagulante dell'impianto, manualmente o mediante un titolatore automatico </p>
              <p> Il LabSense può aiutare a determinare (insieme ai test effettuati con i flocculatori da banco) <strong>il livello ottimale di alcali da dosare nelle applicazioni con acqua dolce</strong>. </p>
              <figure style="display: block; text-align: center;">
                <picture>
                  <source type="image/webp" srcset="<?php echo $root; ?>images/pi-prodotti/strumento-laboratorio-determinare-carica-coagulante.webp">
                  <img src="<?php echo $root; ?>images/pi-prodotti/strumento-laboratorio-determinare-carica-coagulante.jpg" alt="Analizzatore di domanda di coagulante"/>
                </picture>
                <figcaption style="text-align: center;"> Analizzatore e misuratore di carica colloidale </figcaption>
              </figure>
              <p> Il LabsSense è intuitivo, flessibile e fornisce determinazioni ripetibili della <strong>domanda di coagulante</strong>. </p>
              <section>
                <h3>Principio di funzionamento</h3>
                <p> LabSense è uno strumento essenziale per <strong>l’ottimizzazione della coagulazione</strong> nel trattamento delle acque. Lo strumento consente all'utente di determinare il dosaggio di coagulante ideale necessario per ottenere una riduzione ottimale della torbidità e del TOC in meno di 5 minuti. </p>
                <p> La cella di misura è costituita da un pistone alternativo all’interno di un sensore. Le particelle ed i materiali disciolti sono attratti dalle forze di <i>Van Der Waals</i> sulle superfici della sonda. I controioni circondano queste particelle. Il movimento del pistone genera forze di taglio, che causano la <strong> migrazione dei controioni</strong> . Gli elettrodi nella sonda misurano il flusso di controioni, intrinsecamente definito come corrente elettrica. La corrente viene elaborata elettronicamente e visualizzata sullo schermo come <i lang="en">Streaming Current Value (SCV)</i> . Per la maggior parte dei campioni in un impianto di potabilizzazione, SCV sarà negativo, indicando un campione caricato anionicamente netto. </p>
                <p> Il processo di ricerca del dosaggio ottimale comporta semplicemente l'immissione di un volume misurato di coagulante nel campione di acqua grezza fino a quando la lettura indica che è stata ottenuta la <strong> completa neutralizzazione della carica</strong> . </p>
