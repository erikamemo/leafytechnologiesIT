            <article id="Applicazioni">
              <h2>Applicazioni</h2>
              <p> L’analizzatore di corrente fluente (<i lang="en">Streaming Current Monitor</i>) consente
                il controllo effettivo del costo del coagulante fungendo così da: 
              </p>
              <ul>
                <li> analizzatore di controllo della coagulazione nel trattamento delle acque; </li>
                <li> sistema per il controllo automatico della coagulazione. </li>
              </ul>
              <section>
                <h3>Controllo automatico della coagulazione in impianti senza operatori</h3>
                <p> Molti impianti di trattamento delle acque utilizzano ancora il controllo manuale per
                  il dosaggio del coagulante (solitamente sali d'alluminio o ferrico). Finché la
                  <strong>qualità dell'acqua grezza</strong> è stabile, questo è un metodo efficace
                  per gestire il dosaggio del coagulante, tuttavia, quando la qualità dell'acqua
                  grezza cambia, il controllo manuale del coagulante non funziona più, in particolare
                  quando gli impianti non sono controllati durante le ore notturne o nei fine
                  settimana.
                </p>
                <figure style="display: block; text-align: center;">
                  <picture>
                    <source type="image/webp"
                      srcset="<?php echo $root; ?>images/pi-prodotti/misuratore-potenziale-di-flusso.webp">
                    <img src="<?php echo $root; ?>images/pi-prodotti/misuratore-potenziale-di-flusso.jpg"
                      alt="Misuratore di corrente fluente" />
                  </picture>
                  <figcaption style="text-align: center;"> Misuratore di corrente fluente
                  </figcaption>
                </figure>
                <p> Quando ci si aspetta che l'acqua grezza in entrata avrà un contenuto organico
                  elevato (ad esempio, quando si prevede pioggia) gli operatori aumentano il dosaggio
                  di coagulante . <br />
                  Questo può portare ad una scarsa coagulazione prima, durante e dopo la pioggia (se
                  presente) e può anche introdurre <strong>problemi a lungo termine </strong>come il
                  sovraccarico dei filtri, tempi di utilizzo dei filtri più brevi ed un
                  <strong>aumento dei residui di alluminio</strong> (nel caso sia utilizzato come
                  coagulante) .
                </p>
                <p> In molti impianti è possibile automatizzare il controllo del coagulante attraverso
                  la misura della così detta <i lang="en">streaming current</i>, ossia la corrente
                  fluente. Grazie a questa misura si può effettivamente aumentare e diminuire il
                  dosaggio del coagulante in modo automatico in risposta alla variazione della qualità
                  dell'acqua. Questo è particolarmente utile nelle aree con acqua pura ed a bassa
                  alcalinità. 
                </p>
              </section>
