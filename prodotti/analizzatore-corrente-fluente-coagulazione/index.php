              <section>
                <h3>Principio di funzionamento</h3>
                <p> Il campione d'acqua trattata scorre nella cella di campionamento dove viene aspirata
                  e poi espulsa da un pistone in movimento all’interno della cella. Durante questo
                  processo, le particelle contenute nell'acqua vengono temporaneamente
                  <strong>immobilizzate sulle superfici</strong> tra il pistone e la cella.
                </p>
                <p> Lo spostamento in avanti ed indietro dell'acqua fa sì che le cariche che circondano
                  le particelle presenti nell’acqua (positive + e negative -) vengano spostate a
                  <strong>valle degli elettrodi</strong> presenti nella cella. Questo movimento di
                  cariche simili genera una corrente alternata, definita come <em>corrente
                  fluente</em>, ossia, come egregiamente illustrato nell'enciclopedia Treccani:
                </p>
                <blockquote style="margin: 25px; display: block; font-style: italic; background-color:#e3e3e3; padding: 15px; border-left: 5px solid #b4b4b4"
                  cite="http://www.treccani.it/export/sites/default/Portale/sito/altre_aree/Tecnologia_e_Scienze_applicate/enciclopedia/italiano_vol_5/197_204__x3_3_Fenomeni_x.pdf">
                  Quando una soluzione elettrolitica viene fatta scorrere attraverso un capillare per
                  effetto di una differenza di pressione, si riscontra la presenza di una differenza
                  di potenziale elettrico tra due elettrodi posti agli estremi del condotto. Questo
                  potenziale di corrente trova la sua origine nel trasporto da parte della corrente
                  fluida degli ioni presenti nella parte mobile del doppio strato elettrico che si
                  forma in prossimità della superficie del capillare. La differenza di potenziale
                  &#x394;&#x3C6; lungo l’asse del capillare dà luogo a sua volta a una corrente
                  fluente I<sub>s</sub>(<i lang="en">streaming current</i>). <cite><a
                    href="http://www.treccani.it/export/sites/default/Portale/sito/altre_aree/Tecnologia_e_Scienze_applicate/enciclopedia/italiano_vol_5/197_204__x3_3_Fenomeni_x.pdf"
                    target="_blank" title="Apre una nuova finestra">Enciclopedia degli
                  Idrocarburi, 3.3 Fenomeni elettrocinetici, 3.3.7 Potenziale di
                  corrente</a></cite> 
                </blockquote>
                <p> Un <strong>regolatore di guadagno del segnale</strong>, a cui si accede attraverso
                  il menu dello strumento, viene utilizzato per regolare il livello d'amplificazione
                  del segnale. L'amplificazione del segnale deve essere impostata in modo tale che una
                  variazione normale nel dosaggio determini un cambiamento rilevabile e desiderato
                  nella corrente fluente (normalmente 30 unità). Il valore di corrente fluente (<i
                    lang="en">Streaming Current Value o SCV</i>) visualizzato deve essere considerato
                  come una lettura relativa a causa dell'amplificazione del segnale primario. 
                </p>
