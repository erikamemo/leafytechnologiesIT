                  Monitor</i> 
                </figcaption>
              </figure>
              <p> L’analizzatore StreamerSense della <i lang="en">Pi</i> utilizza il principio della
                misurazione della <strong>corrente fluente</strong> per determinare il livello di
                coagulante ottimale per mantenere una carica elettrocinetica (ionica e colloidale)
                adeguata nel <strong>processo di coagulazione</strong> in impianti di potabilizzazione.
              </p>
              <p> La corrente fluente è simile al <strong> potenziale zeta</strong>, o potenziale
                elettrocinetico (indicato come potenziale ζ facendo ricorso alla lettera zeta
                dell'alfabeto greco). Questo è il potenziale generato in seguito alla formazione di un
                doppio strato elettrico ed è responsabile dei fenomeni elettrocinetici e della stabilità
                dei colloidi. (Da Wikipedia) 
              </p>
              <p> Questo <strong>analizzatore di coagulazione</strong> (noto in inglese come <i
                lang="en">Streaming Current Monitor / Streaming Current Detector</i>) misura e
                controlla il livello del coagulante residuo. L'obiettivo è quello di mantenere questo
                residuo al <strong>minimo </strong> assicurando così, allo stesso tempo, la qualità
                dell'acqua finale desiderata e l'efficienza del processo. 
              </p>
              <p> L'analizzatore di coagulazione utilizza la misura della corrente fluente per mantenere
                una <strong>carica elettrocinetica</strong> adeguata (ionica e colloidale) nell'acqua
                trattata. In pratica, risponde ai cambiamenti delle caratteristiche dell'acqua grezza
                (<a href="<?php echo $root; ?>prodotti/turbidimetro-in-linea/">torbidità</a>, colore, <a
                  href="<?php echo $root; ?>prodotti/analizzatore-di-ph/">pH</a>, ecc.) e della
                portata. Ciò consente all'operatore, o ad un dispositivo di controllo automatico, di
                effettuare la <strong>regolazione del dosaggio del coagulante</strong> e mantenerlo
                costantemente al livello ottimale. 
              </p>
              <p> In alcuni impianti di trattamento la chimica dell'acqua è tale che l'analizzatore
                StreamerSense da solo non basti per controllare correttamente la coagulazione. In questi
                casi l'analizzatore di coagulazione può essere integrato dalla <a
                  href="<?php echo $root; ?>prodotti/analizzatore-uv254/">misurazione dell’UV254</a>,
                della <a href="<?php echo $root; ?>prodotti/turbidimetro-in-linea/"> torbidità</a> e del
                <a href="<?php echo $root; ?>prodotti/analizzatore-di-ph/">pH</a> per creare un sistema
                multiparametrico di controllo della coagulazione, noto come <a
                  href="<?php echo $root; ?>prodotti/sistema-controllo-coagulazione/">CoagSense</a>.
              </p>
