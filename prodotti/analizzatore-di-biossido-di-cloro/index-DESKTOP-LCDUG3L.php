            <article id="FAQ">
              <h2>FAQ</h2>
              <!-- <span aria-hidden="true">&#10095;</span> -->
              <dl class="accordion">
                <dt> Qual è la differenza tra il biossido di cloro ed il cloro? <span
                  aria-hidden="true">&#10095;</span></dt>
                <dd> Anche se l'espressione "biossido di cloro" (ClO<sub>2</sub>) include la parola
                  cloro, le due sostanze sono molto diverse. La differenza tra cloro e ClO<sub>2</sub>
                  deriva dalle differenze nella loro struttura chimica. 
                </dd>
                <dt> Perché usare il biossido di cloro anziché il cloro per il trattamento dell'acqua?
                  <span aria-hidden="true">&#10095;</span>
                </dt>
                <dd> Il cloro è un disinfettante molto efficace per disinfettare l'acqua ma anche per
                  mantenere l'acqua potabile disinfettata durante il trasporto. Tuttavia, quando la
                  fonte di acqua potabile è acqua di superficie (proveniente, ad esempio, da fiumi o
                  laghi) e contenente vari materiali organici, il biossido di cloro è la scelta
                  migliore per una serie di motivi: il biossido di cloro viene creato sul posto,
                  eliminando la necessità di stoccare e trasportare il cloro; il biossido di cloro
                  funziona attraverso una <strong>reazione ossidativa</strong> piuttosto che una
                  reazione di clorurazione che <strong>elimina lo sviluppo di composti organici
                  clorurati</strong> che si ritiene aumentino il rischio di cancro. Questi sono
                  chiamati <abbr lang="en" title="Trihalomethanes">THM</abbr> (trialometani) o <abbr
                    title="Disinfection By-Products" lang="en">DBP</abbr> (ossia sottoprodotti
                  della disinfezione). 
                </dd>
                <dt> Come viene utilizzato il biossido di cloro nel trattamento delle acque? <span
                  aria-hidden="true">&#10095;</span></dt>
                <dd> Il biossido di cloro può essere utilizzato come disinfettante principale per le
                  acque di superficie dove ci sono problemi con l'odore ed il gusto. Agisce come un
                  biocida a concentrazioni fino ad 0.1 ppm e su di un ampio intervallo di pH. Il
                  biossido di cloro penetra nella <strong>parete cellulare dei batteri</strong> e
                  reagisce con gli amminoacidi vitali presenti nel citoplasma, eliminando così il
                  microorganismo. Il risultato di questa reazione è il clorito. Il biossido di cloro è
                  più ampiamente usato nel trattamento delle acque industriali ed in applicazioni come
                  il lavaggio degli alimenti ed i circuiti di acqua calda. 
                </dd>
                <dt> Il biossido di cloro è tossico? <span aria-hidden="true">&#10095;</span></dt>
                <dd> Quando gestito correttamente, il biossido di cloro è una sostanza chimica
                  perfettamente sicura da usare. Tuttavia, <strong>come con tutti i prodotti chimici
                  disinfettanti</strong>, se ingerito, bevuto, se si è sottoposti ad esposizione
                  prolungata, se si manipola impropriamente o se viene assorbito attraverso la pelle,
                  può essere tossico. Questo è lo stesso motivo per cui il ClO<sub>2</sub> è un
                  disinfettante efficace. 
                </dd>
                <dt> Il biossido di cloro ha un effetto sull'ambiente? <span
                  aria-hidden="true">&#10095;</span></dt>
                <dd> Il biossido di cloro è ecologico e può essere usato come "difesa contro
                  l'inquinamento". Questo è buono sia per l'ambiente sia per gli esseri umani,
                  infatti, protegge da batteri e dai <strong>sottoprodotti della disinfezione</strong>
                  generati da altri metodi di disinfezione. 
                </dd>
                <dt> Il biossido di cloro può essere conservato in modo sicuro? <span
                  aria-hidden="true">&#10095;</span></dt>
                <dd> Soluzioni di circa l'1% (10 g/l) di ClO<sub>2</sub> possono essere conservate a 5
                  °C per diversi mesi, con effetti minimi sulla concentrazione. La soluzione
                  dev'essere conservata in un contenitore ermetico non esposto alla luce. 
                </dd>
                <dt> Come viene creato il biossido di cloro? <span aria-hidden="true">&#10095;</span>
                </dt>
                <dd> Il biossido di cloro può essere prodotto chimicamente dal clorito di sodio o dal
                  clorato di sodio. Può anche essere generato elettrochimicamente. 
                </dd>
                <dt> Quanto è costoso il biossido di cloro? <span aria-hidden="true">&#10095;</span>
                </dt>
                <dd> In confronto al cloro, il costo di acquisto o fabbricazione del ClO<sub>2</sub> è
                  maggiore. Il costo dipende dal costo dei prodotti chimici originali (<strong>clorito
                  di sodio o clorato di sodio</strong>), dalle sostanze chimiche utilizzate per
                  produrre il biossido di cloro e dalla tecnica di generazione utilizzata.
                  L'attrezzatura per la produzione di biossido di cloro è generalmente meno costosa
                  rispetto a quella di altre opzioni per il trattamento dell'acqua. Nei casi in cui il
                  cloro non sia la scelta migliore, il biossido di cloro risulta essere una buona
                  soluzione. 
                </dd>
                <dt> Qual è la differenza tra un analizzatore, un misuratore ed una centralina di
                  biossido di cloro? <span aria-hidden="true">&#10095;</span>
                </dt>
                <dd> Non vi è alcuna differenza. Sono solo tre espressioni utilizzate in modo
                  intercambiabile da persone diverse. Un analizzatore di biossido di cloro è uno
                  strumento che ha funzionalità di controllo ed analisi. Il DioSense ha tutte queste
                  funzionalità integrate. 
                </dd>
