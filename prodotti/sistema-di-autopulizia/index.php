          <h2>Introduzione</h2>
          <figure style="float: right; clear: left; margin-left: 16px;">
            <picture>
              <source type="image/webp" srcset="<?php echo $root; ?>images/pi-prodotti/sistema-autopulizia-sonde-amperometriche.webp">
              <img src="<?php echo $root; ?>images/pi-prodotti/sistema-autopulizia-sonde-amperometriche.jpg" alt="Sistema per l'autopulizia dei sensori."/> </picture>
            <figcaption style="text-align: right;"> Sistema di Autopulizia </figcaption>
          </figure>
          <p> La nascita di questo sistema che sta rivoluzionando il mondo della misurazione in continuo trova le sue radici nel 2008 quando un cliente in una <strong>cartiera</strong> contattò la Pi chiedendo una soluzione per la misura del pH in linea nel proprio processo. Al tempo il cliente doveva cambiare il sensore di pH ogni settimana incorrendo cosi in un costo enorme. Il sistema di pulizia automatica dei sensori AutoFlush è stato concepito per rispondere a questo problema. </p>
          <p> L'idea alla base del sistema è semplice ed efficace. L’utente definisce ogni quanto la <strong>pulizia automatica</strong> deve essere effettuata, all'inizio del ciclo il campione d’acqua viene rilasciato, la cella di autopulizia ed il sensore vengono puliti grazie ad un getto d’acqua. </p>
          <p> Questo getto d’acqua pulisce sia il sensore che l’alloggiamento del sensore e, in questo modo, evita l’accumulo di solidi, <strong>prolungando cosi la vita del sensore</strong> e riducendo enormemente la necessità di manutenzione continua. </p>
          <p> In caso di accumuli cerosi o grassi, si può utilizzare acqua calda per lavare i sensori e si possono utilizzare delle valvole a manicotto non-standard in caso di acqua carica di solidi o che abbia condizioni difficili. </p>
          <p> Il sistema di autopulizia può essere utilizzato con i sensori pH, redox, cloro libero, cloro totale, ozono, biossido di cloro, acido peracetico e perossido di idrogeno (o una combinazione di sensori) e consiste in una grande cella di flusso che si svuota automaticamente ad <strong>intervalli programmati </strong> selezionabili dall'utente. </p>
          <p> Il segnale dal sensore viene mantenuto e l'intera cella di flusso ed i sensori, vengono lavati con acqua pulita. Grazie a questo sistema, il primo cliente passò da cambiare un sensore di pH ogni settimana a cambiarlo ogni 18 mesi: il miglioramento, quindi, riguarda non solo l'aspetto economico (costi minori) ma anche il controllo di processo. </p>
          <p> Attualmente il sistema è installato in molte cartiere, industrie alimentari e altri stabilimenti dove mantenere puliti i sensori è indispensabile per migliorare le prestazioni e <strong>diminuire i costi</strong>. </p>
