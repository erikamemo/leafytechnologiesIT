        <h2>Cos'è l'acido peracetico e perché viene utilizzato?</h2>
        <p> L'acido peracetico è un potente ossidante con un potenziale di ossidazione elevato,  maggiore di quello di comuni disinfettanti come il cloro ed il biossido di cloro. <strong>L'acido peracetico</strong> viene prodotto dalla reazione tra <strong>l'acido acetico</strong> ed il <strong>perossido di idrogeno</strong> e, una volta disciolto in acqua, si degrada nel tempo formando prodotti solubili <strong>non tossici</strong>. </p>
        <p> Si ritiene che l’acido peracetico distrugga i batteri attraverso il  meccanismo  di <strong>ossidazione  protoplasmatica</strong>, la quale provoca la  disintegrazione della parete cellulare batterica. Il percorso di degradazione più sicuro e l’elevata capacità di ossidazione, rendono l’acido peracetico un disinfettante popolare in molte applicazioni e settori industriali. </p>
