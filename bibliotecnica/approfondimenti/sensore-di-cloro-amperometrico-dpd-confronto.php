        <h2>I sistemi DPD in liea</h2>
        <figure>
          <picture>
            <source type="image/webp" srcset="<?php echo $root; ?>images/pi-approfondimenti/DPD-struttura.webp">
            <img src="<?php echo $root; ?>images/pi-approfondimenti/DPD-struttura.jpg" alt="Struttura del DPD"/> </picture>
          <figcaption>Struttura del DPD</figcaption>
        </figure>
        <p>Questa tecnologia utilizza la reazione chimica tra DPD (N.N-dietil-p-fenilendiammina) ed un ossidante, ad es. cloro libero, per misurare il livello di ossidante. La reazione chimica provoca un <strong> cambiamento visibile</strong> del colore dell’acqua da trasparente a rosa, che è proporzionale al livello di ossidante.</p>
        <p>Il cloro libero viene misurato aggiungendo una quantità misurata con precisione di reagente DPD al campione, insieme ad un tampone stabilizzante per il pH utilizzato per <strong>mantenere il pH tra pH 6.3 e pH 6.6</strong>. Il cloro totale può essere misurato aggiungendo iodio e riducendo il pH a pH 5.1.</p>
        <p>La reazione DPD richiede una misurazione precisa del DPD, del volume del campione e del tampone per garantire che la reazione sia riproducibile ed accurata. Per fare ciò, i sistemi DPD in linea richiedono una misurazione del dosaggio <strong>molto accurata</strong> e tubi sottili per trasportare volumi precisi.</p>
        <p>Per far funzionare correttamente un sistema DPD in linea, è essenziale che lo strumento venga <strong>revisionato ogni anno</strong> con la sostituzione di tutti i tubi. I reagenti utilizzati nella reazione chimica DPD richiedono anche una <strong>sostituzione mensile</strong> che, se combinata con il costo del kit di servizio ed al tempo impiegato per effettuare le attività di manutenzione, significa che i costi di manutenzione sono maggiori rispetto al prezzo di acquisto dello strumento stesso.</p>
        <p>Finalmente molte aziende stanno prendendo in considerazione i <strong> costi totali (Totex)</strong> anziché solo i costi iniziali delle apparecchiature (Capex) e, in questa ottica, i sistemi in linea DPD stanno iniziando ad apparire meno interessanti.</p>
        <h3>Sistemi e sensori amperometrici</h3>
        <figure>
          <picture>
            <source type="image/webp" srcset="<?php echo $root; ?>images/pi-approfondimenti/sistema-amperometrico-sensore.webp">
            <img src="<?php echo $root; ?>images/pi-approfondimenti/sistema-amperometrico-sensore.jpg" alt="Sensore amperometrico"/> </picture>
          <figcaption>Sensore amperometrico</figcaption>
        </figure>
        <p>Questi sistemi usano principi di elettrochimica per misurare la concentrazione di cloro (o altri ossidanti). Si misura  la <strong> corrente prodotta</strong> dalla riduzione del cloro e questa corrente è proporzionale alla quantità di cloro presente. Le sonde amperometriche possono misurare il cloro libero o totale. Molti altri ossidanti come ad esempio <a href="<?php echo $root; ?>prodotti/analizzatore-di-acido-peracetico"> l’acido peracetico</a>, il <a href="<?php echo $root; ?>prodotti/analizzatore-di-biossido-di-cloro/">biossido di cloro</a> e l’<a href="<?php echo $root; ?>prodotti/analizzatore-di-ozono/">ozono</a> possono anch’essi essere misurati con sonde amperometriche specifiche per l’ossidante.</p>
        <p>Tradizionalmente, le sonde amperometriche richiedevano calibrazione a 2 punti, pulizia regolare, compensazione del pH/tamponaggio del pH ed un controllo del flusso estremamente preciso. Ciò significava che richiedevano molta manutenzione o non fornivano risultati accurati. Grazie agli sviluppi tecnologici la situazione è <strong>migliorata notevolmente</strong> .</p>
        <p>Le sonde amperometriche hanno subito molteplici evoluzioni negli ultimi 20 anni e, il risultato di questi cambiamenti, significa che attualmente questi sensori non soffrono di nessuno dei problemi sopraelencati. Le sonde amperometriche che utilizzano tutta la tecnologia disponibile - come i sensori HaloSense - richiedono solo una <strong> calibrazione a punto singolo</strong> poiché la deriva dello zero è stata completamente sradicata.</p>
        <p>In applicazioni con acqua pulita, <strong> non vi è bisogno di ripulire la sonda</strong> . In applicazioni con acque sporche invece, è possibile fornire <a href="<?php echo $root; ?>prodotti/sistema-di-autopulizia/"> sistemi di lavaggio automatico</a> per mantenere pulito e funzionante il sensore. Tutto questo è possibile grazie ai nuovi materiali utilizzati per la membrana.</p>
        <p>Questi, infatti, non richiedono compensazione del pH o tamponaggio, tranne che nelle applicazioni dove il pH è estremo e variabile. Inoltre, le variazioni del flusso sono state ridotte al minimo grazie a miglioramenti del <strong> design delle celle di flusso</strong>.</p>
        <p>Tutti questi miglioramenti significano che i vantaggi che una volta erano il motivo principale per cui scegliere tecnologie di misurazione DPD sono ora <strong>inesistenti</strong> e quindi la tecnologia amperometrica sta diventando comprensibilmente lo standard in quasi tutte le applicazioni di misurazione dell’acqua.</p>
