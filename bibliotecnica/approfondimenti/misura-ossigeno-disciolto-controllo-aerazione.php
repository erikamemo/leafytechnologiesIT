        <p> I batteri all’interno delle vasche di ossidazione necessitano di livelli di ossigeno disciolto sufficienti per sopravvivere e per mantenere un <strong>metabolismo attivo</strong>. </p>
        <p> Se i compressori ed i soffianti immettono troppo ossigeno, si spreca energia elettrica e si promuove la crescita biologica a  livelli indesiderati all’interno del processo. D’altro canto, se non vi è abbastanza ossigeno disciolto, <strong>i batteri potrebbero morire</strong> con  il  conseguente arresto del processo e costi elevati per l’operatore (sostituzione dei fanghi attivi). </p>
        <h2 style="clear: both;">Caratteristiche essenziali in un analizzatore di ossigeno disciolto</h2>
        <p> Affidabilità, robustezza e riproducibilità della misurazione sono probabilmente i tre fattori più importanti di cui un depuratore ha bisogno per <strong>ottimizzare il trattamento dei fanghi attivi</strong>, ossia controllare l’ossigeno disciolto nelle vasche di aerazione in maniera precisa e accurata. </p>
        <p> Tipicamente un processo di aerazione avrà varie vasche con più di un sensore di ossigeno disciolto. Se questi misuratori di ossigeno disciolto riportano livelli molto diversi l’uno dall’altro, diventa difficile per un impianto sapere come gestire il controllo del processo. </p>
        <p> Una delle limitazioni della tecnologia ottica è il graduale abbassamento della lettura nel corso del tempo, quindi è fondamentale che un sensore di ossigeno disciolto sia <strong>affidabile, robusto e riproducibile </strong> in maniera tale da offrire letture coerenti nel tempo ed eliminare/ridurre le preoccupazioni dell’operatore. </p>
        <h3 style="clear: both;">Resistenza all'abrasione</h3>
        <figure>
          <picture>
