        <p> Il luminoforo del sensore di ossigeno disciolto utilizzato dalla Pi è probabilmente uno dei più robusti esistenti al mondo. Il luminoforo è il disco collocato sulla punta del sensore ed utilizzato per rilevare la <strong>quantità di ossigeno presente nell’acqua</strong>. Ovviamente, essendo utilizzato in acque reflue e di processo, dev'essere in grado di sopportare l’ambiente aggressivo in cui funziona. </p>
        <p> Di seguito è riportato un grafico che mostra prima <strong> quanto a lungo</strong> funziona il sensore  quando è sottoposto ad un processo di SABBIATURA (ovviamente a fini sperimentali) e dopo   viene comparato  ad  altri due sensori di fornitori leader a livello mondiale. I risultati sono sconcertanti: il sensore della Pi  dura più di 20 volte di più. </p>
        <h3>Sistema di pulizia automatica</h3>
        <p> Gli addetti alla manutenzione dei sensori di ossigeno disciolto sono abituati ad effettuarne la pulizia con frequenza settimanale. In un impianto di grosse dimensioni, questo significa un grande <strong>spreco di tempo</strong> e di risorse umane soprattutto quando esistono soluzioni di pulizia automatica dei sensori. </p>
        <p> Il sistema OxySense può essere dotato di un cappuccio di pulizia automatica che, utilizzando un getto d'aria compressa, mantiene il <strong> luminoforo pulito</strong> ed evita incrostazioni e crescita biologica. </p>
        <h3>Sistema di autoverifica</h3>
        <p> La procedura  tradizionale  prevede  che  un  operatore  estragga il sensore dalla vasca di aerazione e osservi la risposta del  sensore esposto all’aria. Una volta estratto dalla vasca e terminata la verifica, dunque, l’operatore provvede a pulire e, se necessario, <strong>ricalibrare</strong> il sensore. </p>
        <p> Data la natura delle acque reflue ed il tempo che questa operazione comporta sarebbe desiderabile effettuare questa <strong>verifica in  maniera automatica</strong> e senza la necessità di estrarre il sensore dalla vasca di aerazione. Grazie al sistema di autopulizia è possibile effettuare una verifica automatica del funzionamento del sensore. </p>
        <p> Nei depuratori, i livelli di ossigeno disciolto sono mantenuti relativamente bassi per ridurre i consumi elettrici. Quando il sistema di autopulizia genera un getto d’aria sul luminoforo, questo viene esposto ad un ambiente carico di ossigeno e le letture riportate dal sensore <strong>aumentano considerevolmente</strong>. In questo modo non solo si effettua la pulizia automatica ma si verifica anche che il sensore stia ancora rispondendo correttamente alle concentrazioni di ossigeno disciolto nell’acqua. Di seguito un video di dimostrazione: </p>
