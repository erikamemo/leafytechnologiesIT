        <p> Inizialmente la misurazione dell'ossigeno disciolto nelle acque reflue era effettuata attraverso celle elettrochimiche che necessitavano di elettrodi sacrificali, di elettrolita, di membrane e di una calibrazione frequente. Sebbene esistano ancora alcune aziende che sostengono l'utilizzo di questa tecnologia, a partire dagli anni '90, la <strong>misurazione ottica in linea ed in continuo</strong> ha iniziato a sostituire la tecnologia galvanica. </p>
        <p> La stragrande maggioranza dei sistemi per la misura dell’ossigeno disciolto in linea (anche noti come ossimetri) utilizzati oggi, si basa su tecnologia ottica. </p>
        <h2 style="clear: both;">L'importanza del controllo degli aeratori e dei compressori</h2>
        <figure>
          <picture>
            <source type="image/webp" srcset="<?php echo $root; ?>images/pi-prodotti/diagramma-sensore-ossigeno-disciolto.webp">
            <img src="<?php echo $root; ?>images/pi-prodotti/diagramma-sensore-ossigeno-disciolto.jpg" alt="Diagramma sistema ossigeno disciolto"/> </picture>
