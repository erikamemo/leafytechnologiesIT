        <p> Una versione più completa del contenuto presente in questa pagina è disponibile nella nota tecnica in PDF: <a href="<?php echo $root; ?>docs/note-tecniche/misura-ossigeno-disciolto-controllo-aerazione.pdf">"Misura dell’ossigeno disciolto nelle vasche di aerazione"</a>. </p>
        <h2 style="clear: both;">Metodi di misurazione: principio galvanico e principio ottico </h2>
        <figure>
          <picture>
            <source type="image/webp" srcset="<?php echo $root; ?>images/pi-prodotti/sensore-ossigeno-disciolto.webp">
