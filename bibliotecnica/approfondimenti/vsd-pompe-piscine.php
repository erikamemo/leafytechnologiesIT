        <p>L'articolo spiega come il controllo dell'azionamento a velocità variabile (<abbr lang="en" title="Variable Speed Drive">VSD</abbr>) delle pompe sia collegato al risparmio energetico nelle piscine</p>
        <p>Un <strong>azionamento a velocità variabile (<abbr lang="en" title="Variable Speed Drive">VSD</abbr>)</strong> è un dispositivo elettronico che permette di controllare il motore della pompa, aumentando o riducendo la velocità della stessa.</p>
        <p>La maggior parte delle pompe di ricircolo dell’acqua delle piscine sono <strong>sovra-specificate</strong> per il servizio richiesto e, come tale, è possibile utilizzare un azionamento a velocità variabile per diminuire i consumi energetici e risparmiare notevoli quantità di denaro sui costi dell'elettricità.</p>
        <p>Alcuni punti da evidenziare sono i seguenti:</p>
        <ul>
          <li>il VSD ha permesso di risparmiare <strong>fino al 35%</strong> sulle bollette dell'elettricità ad alcune piscine;</li>
          <li>il VSD permette di risparmiare denaro durante la notte e anche <strong>durante il giorno </strong>quando le tariffe elettriche sono più elevate;</li>
          <li>il controllo VSD è disponibile su entrambi i nostri analizzatori: quello <a href="#">base</a> e quello <a href="#">avanzato</a>.</li>
        </ul>
        <h2>Qual è il problema?</h2>
        <p><strong>Bollette elettriche elevate e impronta di carbonio (di CO<sub>2</sub>) elevate</strong></p>
        <p>La maggior parte delle piscine e dei centri benessere spendono somme importanti in elettricità. Questa è generalmente utilizzata per far funzionare le pompe di ricircolo dell'acqua della piscina, pompe che sono spesso <strong>sovradimensionate</strong>, funzionanti a pieno regime e soprattutto attive al massimo anche quando ci sono pochi bagnanti che utilizzano la piscina o l’impianto termale durante il giorno. </p>
        <p>A causa delle pompe di ricircolo che funzionano continuamente ed a piena potenza, le bollette dell'elettricità mensile possono essere <strong>generalmente elevate</strong>. Il risultato di questa  domanda elevata di elettricità è una maggiore impronta di carbonio della piscina o dell’impianto. I sistemi di controllo VSD attualmente disponibili sul mercato abbassano la velocità della pompa per ridurre il ricircolo dell'acqua della piscina al minimo solamente durante la notte quando non vi sono bagnanti.</p>
        <h2>La soluzione Pi</h2>
        <p>Ciò di cui si ha bisogno è un controllo VSD che oltre a ridurre il consumo energetico durante la notte può anche ridurre i costi durante il giorno <strong>abbassando il regime delle pompe </strong>quando vi sono pochi bagnanti.</p>
        <p>Gli analizzatori della Pi  sono concepiti per utilizzare il controllo VSD sia durante il giorno che durante la notte. In questo modo piscine e centri benessere possono trarre vantaggio dall'abbassamento del regime delle pompe anche durante il giorno ossia quando i costi dell'elettricità sono  più elevati.</p>
        <h2>Come funziona il controllo VSD?</h2>
        <figure>
          <picture>
            <source type="image/webp" srcset="<?php echo $root; ?>images/pi-approfondimenti/controllo-invertitore-grafico.webp">
            <img src="<?php echo $root; ?>images/pi-approfondimenti/controllo-invertitore-grafico.jpg" alt="Grafico Controllo Invertitore"/> </picture>
          <figcaption>Controllo dell'invertitore </figcaption>
        </figure>
        <p>Durante i periodi di utilizzo della piscina, il controllo VSD,  gestisce automaticamente il motore della pompa di ricircolo in base alla <strong>domanda di cloro</strong>. La domanda di biocida (cloro) è direttamente collegata al numero di persone che utilizzano la piscina o il centro benessere in un dato momento, pertanto <a href="<?php echo $root; ?>prodotti/centralina-per-piscina/">l’analizzatore AquaSense</a> può ridurre la velocità della pompa ed i costi associati all’elettricità durante i periodi in cui vi è un minor numero di bagnanti.</p>
        <p> Il grafico mostra il <strong>funzionamento del controllo VSD</strong>, basato sulla domanda di biocidi. Guardando al grafico è possibile osservare come le soglie siano impostate a diversi livelli di cloro e come queste soglie vengano quindi utilizzate per creare bande di livello del biocida, assegnando a ciascuna banda una specifica velocità della pompa. </p>
        <p>In questo modo si <strong>ottimizza il ricircolo</strong> dell'acqua mantenendo il ricambio dell'acqua minimo fissato dal produttore o dal consulente al momento dell'installazione. Durante i periodi diurni in cui il numero di utenti della piscina fluttua, il carico della piscina aumenta e diminuisce.</p>
        <p>Poiché la domanda di biocida è direttamente proporzionale al numero di bagnanti, un numero minore di utenti si tradurrà in livelli di biocida che scendono al di sotto della soglia, consentendo in tal modo una ridotta velocità della pompa durante il giorno. Il controllo VSD della centralina sfrutta questa relazione tra <strong>domanda di biocida e carico di bagnanti </strong> regolando la velocità delle pompe durante il giorno e portando così a risparmi sull'elettricità.</p>
        <p>Piscine ed impianti termali, oltre a risparmiare denaro sull'elettricità, stanno effettivamente riducendo le emissioni di CO<sub>2</sub> e l'impronta di carbonio del proprio stabilimento. Per molte organizzazioni, ridurre le emissioni di CO<sub>2</sub> è un fattore significativo nella scelta delle attrezzature da acquistare. </p>
