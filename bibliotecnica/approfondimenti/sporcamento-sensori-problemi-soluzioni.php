      <h2 style="clear: both;">Il problema: lo sporcamento e l'incrostazione del sensore</h2>
      <figure>
        <picture>
          <source type="image/webp" srcset="<?php echo $root; ?>images/pi-approfondimenti/incrostamento-sensori-problema.webp">
