      <h3>Autoverifica</h3>
      <p> Se si utilizza dell'aria per pulire un sensore di ossigeno disciolto, il sistema può anche <strong>verificare automaticamente</strong> che il sensore stia ancora rispondendo correttamente, eliminando la necessità di rimuovere e di verificare manualmente il sensore. Questo può permettere di andare avanti per mesi senza dover effettuare alcun tipo di operazione. <br/>
