      <p> Qualunque sia il processo monitorato, spesso nell'acqua vi è la presenza di sostanze che possono sporcare il sensore e quindi causare <strong>risultati errati</strong>. Questo è soprattutto vero per i sensori a membrana, ottici ed in generale per tutte le tecnologie che entrano in contatto con l'acqua. </p>
      <p> La soluzione ovvia a questo problema è pulire il sensore, ma questo comporta spesso programmi di manutenzione e di pulizia che possono diventare<strong> onerosi</strong> per gli operatori soprattutto quando pensiamo al fatto che alcuni siti possono avere decine e decine di strumenti. </p>
      <h2 style="clear: both;">La soluzione</h2>
      <p> Le soluzioni che la <i lang="en">Leafy Technologies</i> propone sono i sistemi di autopulizia ed autolavaggio. Questi sistemi sono <strong>semplici, affidabili e di facile manutenzione</strong> e si presentano come un'alternativa ai sistemi di pulizia meccanica che possono ostruirsi e rompersi. </p>
      <p> Spruzzando regolarmente il sensore o la sonda con acqua pulita o aria compressa, il sensore rimane pulito e privo di incrostazioni durante lunghi intervalli. Il ciclo di pulizia del sensore viene attivato dall'<a href="<?php echo $root; ?>prodotti/analizzatori-multiparametrici-multicanale/">analizzatore</a> per un intervallo di tempo e con frequenza <strong>selezionabile dall'utente</strong>, in modo che, indipendentemente da quanto l'applicazione sia sporca, la sonda rimanga pulita. </p>
      <p> Grazie all'assenza di parti mobili nel sensore e nel dispositivo di pulizia, non vi è nulla da sostituire o controllare se non una semplice valvola posizionata in una posizione <strong>facilmente raggiungibile</strong> . </p>
      <p> I sistemi di autopulizia permettono al sistema di funzionare in condizioni difficili per settimane o anche mesi senza alcun bisogno di calibrazione. Come vedremo di seguito vi è una soluzione per ogni applicazione. </p>
