        <p> L'analizzatore di UV254 si basa su di una tecnologia ottica innovativa che, grazie a migliorie tecniche ed operazionali, riduce notevolmente la deriva della misura tradizionalmente dovuta alle <strong>variazioni della sorgente luminosa</strong>. Per questo motivo l'UV254Sense non ha bisogno di effettuare lo "zero", ossia di calibrare lo zero con acqua distillata. </p>
        <p> Una sorgente di luce ultravioletta emessa alla lunghezza d'onda di 254 nm viene fatta passare attraverso il campione d'acqua e, a seconda della composizione chimica dell'acqua (soprattutto torbidità e materia organica), il rilevatore rileva più o meno luce. Grazie a questa misura si determina la <strong> quantità di sostanze organiche</strong> nell'acqua. </p>
        <p> Questa misura può essere espressa come <abbr lang="en" title="Ultraviolet transmittance">UVT</abbr> (trasmittanza ultravioletta) o <abbr lang="en" title="Ultraviolet absorbance">UVA</abbr> (assorbanza ultravioletta), due valori matematicamente correlati. La quantità di UV254 assorbita è dovuta, in gran parte, alle <strong> sostanze organiche</strong> presenti nell'acqua. Le stesse sostanze organiche che costituiscono una grande parte del <strong> TOC</strong> e della <strong> domanda di coagulante</strong> dell'acqua. </p>
        <h2>UV254 per il controllo della coagulazione</h2>
        <figure>
          <picture>
            <source type="image/webp" srcset="<?php echo $root; ?>images/pi-prodotti/misuratore-flusso-CES.webp">
            <img src="<?php echo $root; ?>images/pi-prodotti/misuratore-flusso-CES.jpg" alt="Misuratore di corrente fluente"/> </picture>
          <figcaption> Misuratore di corrente <br/>
            fluente </figcaption>
        </figure>
        <p> La dose di coagulante dipende in larga parte dal pH, dalla torbidità, dalla temperatura, dall'alcalinità ed anche dalle <strong>sostanze organiche</strong>. A seconda della composizione chimica dell'acqua, queste variabili possono avere maggiore o minore peso e, per molti impianti, il fattore determinante è rappresentato dal carico di sostanze organiche. </p>
        <p> Partendo da questo presupposto è possibile comprendere come un controllo avanzato basato sul carico organico o sull'assorbanza UV254 fornisca un sistema  di controllo robusto della coagulazione che può essere ulteriormente migliorato con l'aggiunta di un <a href="<?php echo $root; ?>prodotti/analizzatore-corrente-fluente-coagulazione/" lang="en"> analizzatore di corrente fluente </a>. </p>
        <p> Per una lettura approfondita sul controllo della coagulazione fare riferimento alla nota tecnica <a href="<?php echo $root; ?>docs/note-tecniche/controllo-coagulazione-con-streaming-current.pdf">Controllo automatico  della coagulazione</a>. </p>
        <h2>UV254 come surrogato per analizzatori di TOC</h2>
        <p> Il monitoraggio del TOC in linea è generalmente costoso, ha bisogno di reagenti, di molta manutenzione ed un certo grado di esperienza specialistica che può essere assente negli impianti di trattamento. In molte applicazioni dove sia necessario effettuare una misurazione del TOC un analizzatore UV254 risulta più economico, facile da usare e richiede con meno manutenzione. È possibile creare una <strong>semplice correlazione</strong> lineare tra il TOC e l'UV254 che consente ad un analizzatore di UV254 di produrre direttamente una lettura del TOC in ppm o mg/l. </p>
        <p> Fare riferimento alla nota tecnica <a href="<?php echo $root; ?>docs/note-tecniche/correlare-uv254-parametri-organici.pdf">Correlazione  tra  UV254  e  TOC,  COD e BOD</a>, per una descrizione approfondita. </p>
        <h2>UV254, trialometani e sottoprodotti della disinfezione</h2>
        <p> L'UV254 viene assorbito più fortemente dagli stessi composti organici aromatici che si legano con il cloro per formare THM e <abbr title="Disinfection By-products" lang="en">DBP</abbr> (sottoprodotti della disinfezione) che sono una fonte di preoccupazione crescente. Monitorando l'acqua in entrata ed in uscita da un impianto di trattamento delle acque, l'ingegnere del processo idrico può monitorare quanto bene il processo stia rimuovendo i precursori THM e DBP, ed in caso di problemi, prendere le contromisure  appropriate. </p>
        <h2>La soluzione UV254Sense</h2>
        <p> Il sistema <a href="<?php echo $root; ?>prodotti/analizzatore-uv254/">UV254Sense</a> permette di effettuare la misurazione in continuo dell'UVT/UVA nell'acqua, può essere equipaggiato con un <a href="<?php echo $root; ?>docs/note-tecniche/sistema-filtrazione-uv254.pdf">sistema di filtrazione automatica del campione </a> e con un sistema di autopulizia ed inoltre, è gestito da un analizzatore multiparametrico che permette di gestire più parametri contemporaneamente. Per maggiori informazioni fare riferimento alla documentazione riportata alla fine della pagina. </p>
