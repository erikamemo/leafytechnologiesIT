        <p> Nel seguente articolo si descrivono alcune delle caratteristiche e delle applicazioni degli analizzatori UV254. </p>
        <p> Gli <a href="<?php echo $root; ?>prodotti/analizzatore-uv254/">analizzatori UV254</a> sono normalmente usati per<strong> controllare i sistemi di disinfezione UV</strong> e per il controllo della disinfezione dell'acqua come descritto nella nota tecnica <a href="<?php echo $root; ?>docs/note-tecniche/misura-uvt-controllo-disinfezione-uv.pdf">Trasmittanza  UV  e  disinfezione  con luce ultravioletta</a>. </p>
        <p> Vi sono però altri utilizzi per questa tecnologia: </p>
        <ul>
          <li> gli analizzatori UV254 sono un surrogato fantastico per gli <strong>analizzatori di <abbr lang="en" title="Total Organic Carbon">TOC</abbr></strong>: hanno un costo di acquisto e di gestione nettamente inferiori ed inoltre, la correlazione, è generalmente adeguata per applicazioni dove non vi è bisogno di aderire strettamente ad una normativa specifica; </li>
          <li> gli analizzatori UV254 possono essere utilizzati per identificare i <strong>sottoprodotti della disinfezione</strong> ed i trialometani (<abbr title="Trihalomethanes" lang="en">THM</abbr>); </li>
          <li> i misuratori di assorbanza UV254 sono estremamente utili nel controllare il <strong>dosaggio del coagulante</strong> in base alla misura effettuata dall'analizzatore UV. </li>
        </ul>
        <p> Alla fine di questa pagina si riporta tutta la letteratura della <i lang="en">Leafy Technologies</i> riguardante questi argomenti. </p>
        <h2>Assorbanza e trasmittanza UV254</h2>
        <figure>
          <picture>
            <source type="image/webp" srcset="<?php echo $root; ?>images/pi-prodotti/analizzatore-uv254-autopulizia.webp">
            <img src="<?php echo $root; ?>images/pi-prodotti/analizzatore-uv254-autopulizia.jpg" alt="Sistema di autopulizia per il sensore UV254"/> </picture>
