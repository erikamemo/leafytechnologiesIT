        <p> Quando si parla di disinfezione dell'acqua, l'uso del cloro (ipoclorito) è uno dei primi argomenti affrontati, tuttavia, il cloro non è l’unico disinfettante. Per questo motivo esistono analizzatori e sensori per misurare altre sostanze quali il <a href="<?php echo $root; ?>prodotti/analizzatore-perossido-idrogeno/">perossido di idrogeno</a>, <a href="<?php echo $root; ?>prodotti/analizzatore-di-acido-peracetico/">l’acido peracetico</a>, <a href="<?php echo $root; ?>prodotti/analizzatore-di-biossido-di-cloro/">il biossido di cloro</a> e <a href="<?php echo $root; ?>prodotti/analizzatore-di-ozono/">l’ozono</a> per citarne alcuni. </p>
        <p> Nello specifico il biossido di cloro, anziché essere semplicemente un'alternativa al cloro, può essere più adatto ed efficace in molti settori ed applicazioni. Questo approfondimento contiene una revisione delle tecnologie esistenti ed esamina le proprietà e gli usi del <strong>biossido di cloro</strong> così come i metodi utilizzati per misurare la sua concentrazione in acqua. </p>
        <h2>Cos'è il biossido di cloro?</h2>
        <figure>
          <picture>
            <source type="image/webp" srcset="<?php echo $root; ?>images/pi-approfondimenti/struttura-del-cloro.webp">
            <img src="<?php echo $root; ?>images/pi-approfondimenti/struttura-del-cloro.jpg" alt="Struttura del biossido"/> </picture>
