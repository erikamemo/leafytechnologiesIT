        <p> In linea di principio simili ai sensori a membrana, gli elettrodi in questi strumenti sono direttamente esposti all'acqua del campione. Sebbene ciò comporti alcuni svantaggi rispetto ai sensori a membrana, questo permette di utilizzarli in determinate applicazioni dove altrimenti non sarebbero possibile. </p>
        <p> <b>Vantaggi:</b> a causa della mancanza di membrana ed elettrolita, questi sensori possono essere utilizzati in applicazioni ad alta pressione e alta temperatura. </p>
        <p> Costi di manutenzione ancora più ridotti a causa dell'assenza di cappucci con membrana, elettrolita e reagenti. </p>
        <p> <b>Svantaggi:</b> l’assenza di una membrana microporosa che separi gli elettrodi dal flusso, rende questi sensori molto più sensibili alle interferenze. </p>
        <p> I sensori senza membrana sono molto sensibili alla portata, quindi questa deve essere controllata in maniera precisa. Se la portata cambia, il sensore deve essere ricalibrato di conseguenza un flusso irregolare può renderli quasi inutili. </p>
