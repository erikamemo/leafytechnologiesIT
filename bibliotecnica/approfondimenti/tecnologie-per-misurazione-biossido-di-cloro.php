        <p> Proprio come tutti i processi di disinfezione che avvengono a livello di impianto, la necessità di bilanciare la corretta disinfezione e l'efficacia dei costi indica che è importante disporre di metodi accurati e affidabili per effettuare la misura del biossido di cloro nell'acqua. </p>
        <p> A tale scopo sono stati sviluppati diversi metodi, basati su una varietà di principi diversi. Questi includono: </p>
        <ul>
          <li> titolazione iodometrica; </li>
          <li> spettrofotometria; </li>
          <li> colorimetria; </li>
          <li> amperometria - a membrana e non a membrana. </li>
        </ul>
        <h2>Titolazione iodometrica</h2>
        <p> Questo metodo si basa sull'ossidazione effettuata dal ClO<sub>2</sub> degli ioni di iodio, che può quindi essere titolato con tiosolfato di sodio. È quindi possibile eseguire alcuni calcoli relativamente semplici e dedurre la concentrazione di biossido di cloro. </p>
        <p> <b>Vantaggi:</b> la regolazione del pH consente all'utente di distinguere tra Cl<sub>2</sub> (cloro), ClO<sub>2</sub> (biossido di cloro), ClO<sub>2</sub><sup>-</sup> (clorito) e ClO<sub>3</sub><sup>-</sup> (clorato). Ciò rende il metodo iodometrico ideale per misurare diverse specie di cloro. </p>
        <p> <b>Svantaggi:</b> le titolazioni possono essere difficili e dispendiose in termini di tempo e sono richieste competenze tecniche di livello superiore rispetto ad altri metodi. </p>
        <p> Il biossido di cloro, il clorito e l'ipoclorito <strong> non si distinguono facilmente</strong>, il che può causare problemi in alcune applicazioni. </p>
        <p> Possono verificarsi errori significativi se è presente più di una specie. </p>
        <p> Sebbene disponibile nel formato di un test kit, il metodo iodometrico non è stato adattato per l'analisi in linea in continuo. </p>
