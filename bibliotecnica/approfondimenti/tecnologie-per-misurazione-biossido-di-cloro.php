            di cloro </figcaption>
        </figure>
        <p> Il biossido di cloro è un composto con la formula ClO<sub>2</sub>, appare come un gas verde-giallastro che non idrolizza in acqua, mantenendo la sua struttura come gas disciolto in soluzione. Si tratta di un potente ossidante ed era stato inizialmente suggerito come agente sterilizzante nel 1900, ma il suo <strong>utilizzo su grande scala</strong> negli impianti di trattamento è iniziato solo dal 1944. </p>
        <p> Le proprietà ossidanti del biossido di cloro lo rendono utile per la disinfezione e, in genere, presenta numerosi vantaggi rispetto al cloro: ad esempio, è più efficace su agenti patogeni specifici come <strong>Legionella o Giardia</strong>. </p>
        <p> Se usato correttamente, il biossido di cloro non solo produce <strong>sottoprodotti meno dannosi </strong>del cloro, inoltre, la sua capacità di disinfezione non è influenzata dal pH. Grazie alle sue proprietà chimiche può risultare più conveniente perché sono necessarie concentrazioni più basse rispetto ad altri disinfettanti. </p>
        <h2>Gli usi del biossido di cloro</h2>
        <p> Sebbene storicamente il biossido di cloro sia stato percepito come pericoloso da gestire e, in forme concentrate e gassose, certamente può esserlo, le tecnologie moderne di generazione in loco consentono di utilizzarlo con maggiore sicurezza. </p>
        <p> Per questo motivo il biossido di cloro ha trovato il suo posto in numerose applicazioni: </p>
        <ul>
          <li> <strong>disinfezione dell'acqua</strong> (liquami, processi industriali, torri di raffreddamento, prodotti alimentari ecc.); </li>
          <li> <strong>sbiancamento del legno, pasta di legno e carta</strong>; </li>
          <li> <strong>rimozione del biofilm</strong> grazie alla sua capacità di penetrare negli strati di polisaccaridi delle cellule batteriche; </li>
          <li> <strong>ossidazione di ferro e manganese solubili, per facilitare la rimozione</strong>. </li>
        </ul>
