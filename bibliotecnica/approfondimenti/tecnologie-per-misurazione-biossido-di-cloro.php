        <h3>Colorimetrico</h3>
        <p> L'analisi colorimetrica si basa sulla reazione tra il ClO<sub>2</sub>, un colorante e sulla misurazione dell'assorbanza di una specifica lunghezza d'onda della luce dopo questa reazione. I coloranti che possono essere usati includono N, N-dietil-p-fenilendiammina (DPD), clorofenolo rosso (CPR) e lissamina verde (LGB). Ognuno di questi coloranti ha i suoi limiti, ad esempio, il colore del DPD cambia nel tempo, la LGB dipende dalla temperatura), ma hanno anche caratteristiche positive. </p>
        <p> <b>Vantaggi:</b> se eseguita correttamente la colorimetria offre potenzialmente una maggiore precisione rispetto alla spettrofotometria, inoltre questa tecnologia è conosciuta dalla maggior parte dei tecnici e del personale dei siti di trattamento. </p>
        <p> I prodotti di colorimetria in linea sono disponibili da una varietà di fabbricanti. </p>
        <p> <b>Svantaggi:</b> l’intervallo di funzionamento è limitato dalla concentrazione del colorante, che pone vincoli fisico-chimici su ciò che è possibile misurare. </p>
        <p> La purezza dei coloranti può essere un problema, portando ad inesattezze. Alcuni studi riportano la purezza della stessa tintura che varia dal 45% al 95%. </p>
        <p> Alcuni coloranti non sono adatti alle misurazioni in tempo reale a causa dei tempi di reazione più lunghi. </p>
        <p> I colorimetri in linea richiedono una fornitura continua di reagenti, che può essere costosa. Inoltre, i tubi all’interno del sistema si possono bloccare spesso. </p>
