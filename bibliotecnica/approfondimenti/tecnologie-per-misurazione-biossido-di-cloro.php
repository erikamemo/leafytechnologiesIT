        <h3>Spettrofotometria</h3>
        <p> Questo metodo si basa sulla trasmissione della luce. Una lunghezza d'onda specifica della luce (circa 360 nm nel caso del biossido di cloro) viene fatta passare attraverso una cuvetta contenente il campione e viene misurata la trasmissione/assorbimento. </p>
        <p> Maggiore è la concentrazione di biossido di cloro, minore è il livello di trasmissione; questo costituisce la base della misurazione. </p>
        <p> <b>Vantaggi:</b> questo metodo è relativamente <strong>privo di interferenze</strong> rispetto alla titolazione iodometrica. Gli spettrofotometri sono facili da utilizzare e vi è il potenziale di ottenere livelli elevati di accuratezza. </p>
        <p> <b>Svantaggi:</b> l'accuratezza complessiva dipende dal fotometro utilizzato, con i modelli migliori che costano decine di migliaia di euro. </p>
        <p> Esiste un'interferenza nota con il clorito, che può introdurre livelli significatici di imprecisione. </p>
        <p> Gli spettrofotometri in linea esistono ma non sono progettati pensando al biossido di cloro. </p>
