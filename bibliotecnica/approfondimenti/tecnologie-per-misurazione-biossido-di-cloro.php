        <h3>Sensori amperometrici a membrana (forniti dalla Leafy Technologies e da Pi)</h3>
        <p> Progettati in maniera specifica per le misurazioni in linea, questi sensori basati su elettrodi utilizzano un elettrolita specifico contenuto in un cappuccio con una membrana selettiva. Questa membrana permette alla ClO<sub>2</sub> di diffondersi all’interno dell'elettrolita, dove agli elettrodi avviene quanto segue: </p>
        <ul>
          <li> Catodo: ClO<sub>2</sub> + 4H<sup>+</sup> + 5e<sup>-</sup> → Cl<sup>-</sup> + 2H<sub>2</sub>O </li>
          <li> Anodo: Cl<sup>-</sup> + Ag → AgCl + e<sup>-</sup> </li>
        </ul>
        <p> Il rilascio di elettroni su di un elettrodo e l'accettazione di elettroni sull'altro creano un flusso di corrente tra loro, che costituisce la base della misurazione. </p>
        <p> <b>Vantaggi:</b> la membrana mantiene contaminanti dannosi lontano dagli elettrodi, proteggendoli. </p>
        <p> C'è una bassa dipendenza dalla portata. </p>
        <p> L'elettrolita è predefinito e quindi l'assenza di biossido di cloro non fornisce corrente; questo consente una calibrazione a punto unico. </p>
        <p> Non sono richiesti reagenti o metodi di test complessi, il che significa bassi costi operativi per tutta la durata del sensore. </p>
        <p> Il funzionamento continuo rende questi sensori ideali per la misurazione in linea. </p>
        <p> <b>Svantaggi:</b> tensioattivi e detergenti possono influire sulla membrana, permettendo all'acqua di passare nell'elettrolita e causare la deriva del segnale. </p>
        <p> Sostanze idrorepellenti come gli oli minerali possono ostruire i pori della membrana, quindi il biossido di cloro non può passare nell'elettrolito causando un errore di misurazione. </p>
        <p> L’ozono è un interferente noto. </p>
