        <h3>Concorrente A</h3>
        <p> Il sensore del concorrente A, ha faticato a mantenersi pulito automaticamente tra le manutenzione in quanto il sistema di autopulizia passiva era inefficace. Lo strumento sembra riportare erroneamente <strong>letture più basse del dovuto</strong> a causa di fango e detriti che ricoprono il sensore. Il sensore rileva l'aumento dell'ossigeno disciolto durante l'aerazione, ma poi cade ad una lettura pari a zero per lunghi periodi di tempo. </p>
