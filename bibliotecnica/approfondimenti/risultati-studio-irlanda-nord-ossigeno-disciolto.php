        <p> Vi è un articolo in PDF su questo approfondimento: <a href="<?php echo $root; ?>docs/articoli/test-ossigeno-disciolto-irlanda.pdf">"Risultati di uno studio sul campo sui sensori di ossigeno disciolto"</a>. </p>
        <p> L’azienda responsabile del trattamento dell’acqua in Irlanda del Nord (<i lang="en">Northern Ireland Water</i>) ha  testato tre <strong> analizzatori di ossigeno disciolto</strong> (DO) in parallelo. Questi sono stati installati fianco a fianco nell'impianto di depurazione di Culmor nella stessa vasca di aerazione, dove le misure sono state registrate ogni minuto per 6 mesi. </p>
        <p> Dopo aver elaborato più di un milione di punti dato, il sistema <a href="<?php echo $root; ?>prodotti/analizzatore-di-ossigeno-disciolto/">OxySense</a> è risultato la soluzione migliore. </p>
        <p> Il grafico mostra <strong>un'istantanea di tre ore</strong> con alcuni dei risultati che hanno portato l'azienda a decidere che l'analizzatore di ossigeno disciolto OxySense fosse il migliore in assoluto. Osservando questo grafico è chiaro il perché: il sensore del concorrente A mostra una lettura bassa ed imprecisa e il sensore del concorrente B è influenzato dal rumore. </p>
        <p> Questi risultati sono discussi in maggior dettaglio più avanti . </p>
        <h2>L’impianto</h2>
        <p> L'impianto di prova aveva utilizzato i misuratori di ossigeno disciolto del concorrente B per un po' di tempo in tutte le proprie <strong> vasche di aerazione</strong>, tuttavia, vi era un grosso inconveniente: il cappuccio del sensore (l'elemento che effettua la misurazione) doveva essere sostituito frequentemente (ogni 6 mesi). I costi di sostituzione si aggiravano intorno ai  €110 per sensore hanno iniziato ad accumularsi e l'impianto ha deciso di cercare un'alternativa. </p>
        <h2>Pulizia automatica</h2>
        <figure>
          <picture>
            <source type="image/webp" srcset="<?php echo $root; ?>images/pi-prodotti/cappuccio-autopulente-ossigeno-disciolto.webp">
            <img src="<?php echo $root; ?>images/pi-prodotti/cappuccio-autopulente-ossigeno-disciolto.jpg" alt="Cappuccio autopulente per sensore di ossigeno disciolto"/> </picture>
          <figcaption> Cappuccio autopulente <br/>
            per sensore di <br/>
            ossigeno disciolto </figcaption>
        </figure>
        <p> Il sensore OxySense è stato dotato di un <strong>cappuccio autopulente</strong>, che pulisce il sensore attivamente con aria compressa (o acqua pulita) per rimuovere fanghi e detriti dalla punta del sensore su base regolare. </p>
        <blockquote> "Quando le sonde sono state rimosse dalla vasca era ovvio che il sistema di pulizia della Pi funzionava bene e avrebbe ridotto di molto tempi e costi di manutenzione". </blockquote>
        <p> <i lang="en">Peter Martin</i> , responsabile strumentazione, NIW. </p>
        <p> Il sensore del concorrente A era collegato ad un supporto flessibile che permetteva al sensore di muoversi con il fango e seguendo le bolle di aerazione,<strong> teoricamente</strong> puliva passivamente il sensore. 	Il sensore del concorrente B, invece, non aveva alcuna funzionalità di pulizia automatica. </p>
        <p> Tutti e tre i sensori sono stati puliti manualmente come parte del programma di manutenzione ordinaria dello stabilimento, garantendo che il test fosse equo per i sensori senza un sistema di pulizia. </p>
        <h2>Risultati</h2>
        <p> Le misurazioni effettuate dal 25 maggio 2015 sono state utilizzate per dimostrare la variazione tra i diversi analizzatori. Questa serie di date è stata scelta per essere rappresentativa dell'intero processo. </p>
        <h3>Sensore OxySense</h3>
        <p> Lo studio ha rilevato che il CRIUS<sup>®</sup> <a href="<?php echo $root; ?>prodotti/analizzatore-di-ossigeno-disciolto/">OxySense</a> è il misuratore di ossigeno più accurato ed affidabile tra i tre. Il sensore è stato mantenuto pulito dal sistema di autopulizia e <strong>ha riportato letture accurate e precise con un basso livello di rumore</strong>. Un graduale aumento del contenuto di ossigeno disciolto è facilmente osservabile durante l'aerazione, seguito da un declino costante quando gli aeratori vengono spenti. </p>
