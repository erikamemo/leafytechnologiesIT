        <p> Le pompe sono quasi onnipresenti sia nei processi idrici che in quelli di trattamento delle acque: sono utilizzate per muovere l'acqua o <strong> dosare sostanze chimiche</strong> all’interno di un processo. In genere, su di uno stesso impianto, vi sono pompe di diverse dimensioni e con caratteristiche diverse e, per molti operatori, la gestione di questa complessità risulta una sfida. </p>
        <p> Nell'esaminare la relazione tra analizzatori e pompe bisogna tener presente i tre punti seguenti: </p>
        <ul>
          <li> gli analizzatori odierni offrono molti degli <strong>algoritmi di controllo</strong> necessari al processo di trattamento dell’acqua; </li>
          <li> gli analizzatori della qualità dell'acqua possono ricevere <strong>segnali/notifiche di errore</strong> dalle pompe ed aiutare così a mantenere i sistemi di trattamento sicuri ed efficienti; </li>
          <li> gli analizzatori <a href="<?php echo $root; ?>prodotti/analizzatore-multiparametrico-base/">CRONOS<sup>®</sup></a> e <a href="<?php echo $root; ?>prodotti/analizzatore-multiparametrico-avanzato/">CRIUS<sup>®</sup></a> sono gli unici analizzatori che possono essere collegati tramite protocolli SMART e DIGITAL ad una pompa <strong>E-box DDA della Grundfos</strong>. </li>
        </ul>
        <p> Gli analizzatori si interfacciano e controllano le pompe per svolgere una serie di funzioni differenti elencate di seguito. </p>
        <figure>
          <picture>
            <source type="image/webp" srcset="<?php echo $root; ?>images/pi-prodotti/centralina-analizzatore-per-piscina.webp">
