        <ul>
          <li> <strong>Controllo del dosaggio di sostanze chimiche</strong>: controllo di una pompa per dosare una sostanza chimica in un sistema in base a una o più misurazioni. Ad esempio, si può utilizzare la misura del cloro residuo e la portata, per gestire un controllo PID. </li>
          <li> <strong>Controllo del ricircolo</strong>: si utilizza una pompa per gestire il ricircolo di un sistema. Questa soluzione è comunemente utilizzata nelle piscine, leggere l'approfondimento "<a href="<?php echo $root; ?>bibliotecnica/approfondimenti/vsd-pompe-piscine.php">Controllo dell'azionamento a velocità variabile (VSD) delle pompe ed il risparmio energetico nelle piscine</a>" per maggiori informazioni. </li>
          <li> <strong>Protocolli di sicurezza</strong>: sistemi di trattamento complessi possono smettere di funzionare per una serie di motivi e l’analizzatore gioca un ruolo importante nel renderli più resilienti. Molte pompe trasmettono segnali  di allarme digitali quando rilevano un guasto, notificando così l'analizzatore che, di conseguenza, <strong>blocca il dosaggio</strong> di sostanze chimiche. Quest'ultimo, in un processo di trattamento dell'acqua dove non vi è in ricircolo, può portare, in caso di guasti, ad un sovradosaggio pericoloso ed a sprechi di disinfettante con conseguente aumento dei costi. </li>
          <li> <strong>Rotazione di servizio o backup</strong>: in sistemi di grosse dimensioni le pompe possono essere usate a turno per aumentare la propria longevità o per avere un sistema di backup su cui fare affidamento in caso di guasto di una delle pompe. Avere un backup, o un sistema n+1, può spesso ridurre significativamente i <strong> tempi di inattività</strong>, quindi è molto utile nelle linee di produzione in cui i tempi di inoperosità risultano in costi significativi. </li>
        </ul>
        <p> n+1 è un termine standard utilizzato nei sistemi di ridondanza. La "n" indica il numero di elementi richiesti per far funzionare il sistema, quindi, in questo caso, il numero di pompe; "+1" indica il numero di sistemi di backup in atto. </p>
        <p> Di seguito un riepilogo dei diversi tipi di segnali che possono essere utilizzati per interagire con le pompe, alcune delle applicazioni comuni in cui vengono utilizzati ed i loro vantaggi e svantaggi. </p>
        <h2><a class="chapter" id="controllo-relè">Controllo a relè di commutazione dell'alimentazione</a></h2>
        <p> <b>Descrizione:</b> controllo acceso/spento. Si fornisce alimentazione alla pompa quando deve essere accesa (questo può essere 12-230 V) e non si fornisce alimentazione quando deve essere spenta. </p>
        <p> <b>Vantaggi:</b> il metodo di controllo più economico ed universale. </p>
        <p> <b>Svantaggi:</b> elevata usura della pompa e del relè. </p>
        <p> <b>Comunemente utilizzato per:</b> pompe dosatrici di piccola taglia, piscine, sistemi di trattamento di piccole dimensioni. </p>
        <h2><a class="chapter" id="controllo-vfc">Controllo VFC (<i lang="en">Volt Free Contact</i> / contatto pulito)</a></h2>
        <p> <b>Descrizione:</b> la pompa viene alimentata separatamente dall'analizzatore e inizia a pompare solo quando un circuito di controllo a bassa tensione è chiuso. Un controllo VFC può essere gestito attraverso soglie o con un controllo PID (leggere la Nota Tecnica "<a href="<?php echo $root; ?>docs/note-tecniche/controllo-pid-analizzatori-acqua.pdf">Il controllo PID negli analizzatori in linea dell’acqua</a>") e può essere utilizzato come controllo acceso/spento o modulazione di frequenza/larghezza di impulsi. </p>
        <p> Questo è talvolta chiamato “Controllo ad impulsi” (<i lang="en">Pulse Control</i>). Le pompe progettate per essere controllate da un’uscita ad impulsi di un misuratore di portata possono essere  controllate <strong>anche da un analizzatore</strong> grazie al controllo ad impulsi. In questi casi è come se l’analizzatore stesse fingendo di essere un misuratore di portata. </p>
        <p> <b>Vantaggi:</b> generalmente accettato dalle pompe e protegge sia il relè che la pompa da sovratensioni elevate. </p>
        <p> <b>Svantaggi:</b> non è preciso come il controllo analogico, specialmente nei sistemi dove non vi è ricircolo. </p>
        <p> <b>Comunemente utilizzato per:</b> pompe di dosaggio di piccole e medie dimensioni, piscine. </p>
          <h2><a class="chapter" id="controllo-analogico">Controllo analogico</a></h2>
        <p> <b>Descrizione:</b> un segnale da 4-20 mA o 0-10 V viene trasmesso alla pompa per controllare la velocità della stessa. </p>
        <p> <b>Vantaggi:</b> controllo molto preciso, anche su sistemi dove non vi è ricircolo. La pompa è sempre accesa eliminando i picchi di corrente e l'affaticamento all'avvio. </p>
        <p> <b>Svantaggi:</b> può essere utilizzato solo su pompe che accettano segnali analogici. Un segnale 4-20 mA ha una portata limitata (&lt; 50 m). </p>
        <p> <b>Comunemente utilizzato per:</b> sistemi di dimensioni medio-grandi, piscine di grandi dimensioni, qualsiasi sistema in cui sia necessario un dosaggio preciso. </p>
          <h2><a class="chapter" id="controllo-digital">Controllo SMART / DIGITAL</a></h2>
        <p> <b>Descrizione:</b> sempre più pompe hanno l'opzione di essere gestite via protocolli digitali quali Modbus o Profibus. Questi sono progettati per essere utilizzati direttamente dai PLC e offrono agli operatori vantaggi distinti rispetto ai metodi più tradizionali. Un segnale digitale ha una portata maggiore ed è in grado di trasmettere <strong>informazioni molto più dettagliate</strong> di un semplice segnale di allarme acceso/spento. </p>
