        <p> <b>Comunemente utilizzato per:</b> sistemi da grandi a molto grandi, sistemi che funzionano 24/7 in cui i risparmi sui prodotti chimici risultano essere importanti ed i tempi di fermo ridotti. In particolare, i sistemi di produzione degli alimenti beneficiano di maggiori tempi di produzione e di un'eccellente verificabilità. </p>
