        <p> Un esempio di pompa che supporta il controllo SMART/DIGITAL è l'e-box della Grundfos aggiunto alla pompa di dosaggio DDA. L'E-box fornisce una connessione Modbus o Profibus per la <strong> comunicazione a 2 vie</strong> . </p>
        <p> L’analizzatore CRIUS<sup>®</sup> è l'unico analizzatore in grado di utilizzare <strong> tutte le informazioni</strong> fornite dalla pompa DDA. Informazioni quali: tempo di funzionamento totale della pompa, pressione della pompa, volume pompato e molte altre sono ora disponibili all'analizzatore. </p>
        <p> Le notifiche di errore sono specifiche, fornendo così agli operatori informazioni dettagliate per la risoluzione dei problemi. Questa ricchezza di informazioni può essere utile per coordinare la manutenzione ed aumentare i tempi di attività della pompa e del sistema nel suo complesso. Inoltre, tutte queste informazioni possono essere archiviate e registrate, il che rende il sistema esternamente efficiente ed offre un controllo avanzato. </p>
        <p> Normalmente, tutte queste informazioni sono disponibili solo per operatori con accesso ad un PLC o per un ingegnere PLC in grado di integrare i due sistemi. Tuttavia, il CRIUS<sup>®</sup> è un analizzatore personalizzabile e semplice da usare che non ha bisogno di un ingegnere PLC per la sua configurazione. </p>
        <p> Inoltre, il controllo della pompa effettuato con l’e-box DDA è estremamente preciso: l'uscita della pompa si adatta in maniera precisa alla scala utilizzata dall'analizzatore. </p>
        <p> Ad esempio, una pompa tradizionalmente controllata da un’uscita 4-20 mA che indica alla pompa di andare al 60% della potenza, può avere un errore di 1 litro rispetto al vero 60%. In numeri ciò significa che una pompa da 0-30 l/h, controllata da un segnale 4-20 mA <strong> pompa effettivamente a 19 l/h, mentre una pompa controllata via Modbus pompa 18 l/h</strong> . Questa differenza (piccola ma misurabile), quando  ripetuta durante l’intera vita utile della pompa risulta in  sprechi enormi di sostanze chimiche. Infine, queste differenze aumentano con l’aumentare della lunghezza del cavo da 4-20 mA (e quindi dell’errore). </p>
