        <p> L'articolo descrive come misurare l'assenza di cloro (definito come Zero Cloro), vi è anche una nota tecnica in PDF sull'argomento: <a href="<?php echo $root; ?>docs/note-tecniche/sensore-misura-zero-assenza-cloro.pdf">"Misurare l’assenza di cloro"</a>. </p>
        <p> Se ci si è mai imbattuti in un'applicazione che utilizza l'<strong>osmosi inversa</strong> (<abbr title="Reverse Osmosis" lang="en">RO</abbr>), ad esempio, in un reparto di dialisi, probabilmente si è ben consci dell'effetto dannoso che il cloro può avere sulle membrane ad osmosi inversa. </p>
        <p> Pensando a questo tipo di applicazioni è stato ideato un sistema che genera un allarme qualora vi fosse<strong> una qualsiasi presenza di cloro libero</strong>, in questo modo si può effettivamente impedire all'acqua contaminata di raggiungere le membrane. </p>
        <p> La fonte d'acqua più comune per edifici aziendali, ospedali e processi industriali, è l'acqua proveniente dalla <strong>rete di distribuzione</strong>. Quest'acqua contiene in genere un livello di cloro residuo aggiunto dalle aziende municipali per le sue proprietà disinfettanti. </p>
        <p> In qualsiasi sistema che usi membrane ad osmosi inversa se la fonte d'acqua è la rete municipale allora si è soliti utilizzare <strong>filtri a carbone attivo</strong> posizionati prima delle membrane. I filtri a carbone attivo sono progettati per <strong>rimuovere </strong>qualsiasi sostanza chimica presente nell'acqua in grado di danneggiare le membrane ad osmosi inversa (compreso il cloro). </p>
        <h2>Il sensore per lo Zero Cloro</h2>
