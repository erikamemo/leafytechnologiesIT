        <p> Se il cloro dovesse sfondare i filtri a carbone attivo e passare oltre alle membrane di osmosi inversa, potrebbe mettere in pericolo la vita dei pazienti di dialisi in ospedale. Il sistema di  Zero Cloro permette di <strong>aumentare la sicurezza</strong> del sistema, fornendo un'ulteriore linea di difesa. </p>
