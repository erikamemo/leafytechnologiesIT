        <p> Tutti i turbidimetri hanno difficoltà a gestire <strong>le bolle presenti nell’acqua </strong> perché influenzano le proprietà ottiche dell'acqua in cui si trovano. Per un sensore queste bolle assomigliano alla torbidità. Le bolle possono essere trascinate nell'acqua del campione e, quando la pressione viene ridotta, <strong> i gas disciolti nell'acqua possono nucleare</strong> (quando a contatto con qualsiasi superficie) sotto forma di bolle. Ovviamente, possono nucleare anche sulla finestra ottica del sensore. Le bolle sia nucleate che trascinate possono dare luogo a letture errate, che influiscono sull'affidabilità della misurazione.</p>
