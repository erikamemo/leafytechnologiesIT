		<h2>DEFINIRE ACQUA 4.0, SMART E DIGITAL</h2>
		<p>
			<strong>Acqua 4.0</strong>. Questa espressione descrive semplicemente come questa tecnologia e filosofia sarà implementata ed  influenzerà il settore idrico. Questo significa più sensori, più modellazione, strumenti ed analizzatori più intelligenti, pompe e così discorrendo.
		</p>
		<h3>SMART e DIGITAL</h3>
		<p>
			Questi termini non sono ben definiti come gli altri. Generalmente si accetta che SMART sia la crescente sofisticazione delle  apparecchiature che permette loro di prendere decisioni decentralizzate, ad  esempio, nel 1990 un analizzatore di cloro misurava il cloro in acqua, forniva un’uscita 4-20 mA proporzionale al cloro e magari azionava un allarme con un paio di relè.
		</p>
		<p>
			Nel 2020 <a href="<?php echo $root; ?>prodotti/analizzatore-multiparametrico-avanzato/">l’analizzatore multiparametrico CRIUS<sup>®</sup> </a> (sviluppato per l’Acqua 4.0) è in grado di fornire uscite 4-20 mA e relè, ma ha anche più opzioni di comunicazione digitale quali Modbus e  Profibus (<strong>Interoperabilità</strong>), supporta fino a 16 sensori (<strong>Trasparenza delle informazioni</strong>), offre accesso da Internet con l’accesso remoto (<strong>Assistenza tecnica</strong>) e controlli PID sofisticati gestibili da remoto, vale a dire che un altro dispositivo può gestire l’impostazione dei  valori obiettivo (<strong>Decentramento</strong> ).  In  pratica,  la  tecnologia SMART è la tecnologia che consente l’implementazione di Industria 4.0 o Acqua 4.0.
		</p>

		<p>
			Infine, DIGITAL tende a riferirsi alla capacità di comunicazione dello strumento grazie a protocolli quali Modbus e Profibus.
