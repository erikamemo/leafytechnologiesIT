			La quarta (<strong>Industria  4.0</strong>) riguarda le <strong> fabbriche "SMART"</strong> (intelligenti).

		</p>
		<p>
			L’origine dell’espressione Industria 4.0 è da ritrovarsi in un gruppo di consulenti del governo tedesco che presentò le   proprie idee nel 2012. Attualmente Industria 4.0 significa rendere le fabbriche SMART ossia avere varie risorse (macchine, persone, robot, intelligenza artificiale, tecnologia informatica) che <strong>comunicano tra di loro</strong>. I principi proposti da questi consulenti sono riportati nella lista di seguito.
		</p>
		<ul>
			<li>
				<strong> Interoperabilità</strong>: tutto dovrebbe essere in grado di comunicare con tutto il resto.
